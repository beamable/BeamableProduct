	private static void RegisterLogServices(IDependencyBuilder builder, RecordingRealmConfigService realmConfig, RecordingLoggingContextService loggingContext)
	{
		builder.RemoveIfExists<IRealmConfigService>();
		builder.RemoveIfExists<IMicroserviceRealmConfigService>();
		builder.RemoveIfExists<ILoggingContextService>();
		builder.AddSingleton<IRealmConfigService>(realmConfig);
		builder.AddSingleton<IMicroserviceRealmConfigService>(realmConfig);
		builder.AddSingleton<ILoggingContextService>(loggingContext);
	}

	private class RecordingRealmConfigService : IRealmConfigService
	{
		private readonly LogLevel _level;

		public RecordingRealmConfigService(LogLevel level)
		{
			_level = level;
		}

		public int UpdateLogLevelCount { get; private set; }

		public Promise<RealmConfig> GetRealmConfigSettings()
		{
			return Promise<RealmConfig>.Successful(new RealmConfig(new Dictionary<string, RealmConfigNamespaceData>()));
		}

		public void UpdateLogLevel()
		{
			UpdateLogLevelCount++;
			MicroserviceLogLevelContext.CurrentLogLevel.Value = _level;
		}
	}

	private class RecordingLoggingContextService : ILoggingContextService
	{
		public int GetLogLevelContextCount { get; private set; }

		public Promise<Dictionary<string, BeamoV2ServiceLoggingContext>> GetAllLoggingContexts()
		{
			return Promise<Dictionary<string, BeamoV2ServiceLoggingContext>>.Successful(new Dictionary<string, BeamoV2ServiceLoggingContext>());
		}

		public BeamoV2ServiceLoggingContext GetLogLevelContext(string serviceName, string routingKey)
		{
			GetLogLevelContextCount++;
			return null;
		}
	}

	[Microservice("log_policy", EnableEagerContentLoading = false)]
	public class LogPolicyService : Microservice
	{
		[ClientCallable]
		public string Ping()
		{
			return "ok";
		}
	}
}
