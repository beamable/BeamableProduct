using Beamable.Server.Api.RealmConfig;
using Microsoft.Extensions.Logging;
using NUnit.Framework;
using System;
using System.Collections.Generic;
using System.Threading.Tasks;
using ClientRequest = Beamable.Microservice.Tests.Socket.ClientRequest;

namespace microserviceTests.microservice.dbmicroservice.BeamableMicroServiceTests;

[TestFixture]
public class AdminRouteLogPolicyTests : CommonTest
{
	private const string AdminRouteLogPolicyEnvVar = "BEAM_ADMIN_ROUTE_LOG_POLICY";

	[TestCase(null, AdminRouteLogPolicy.Protected)]
	[TestCase("", AdminRouteLogPolicy.Protected)]
	[TestCase("Protected", AdminRouteLogPolicy.Protected)]
	[TestCase("FollowServiceRules", AdminRouteLogPolicy.FollowServiceRules)]
	[TestCase("followservicerules", AdminRouteLogPolicy.FollowServiceRules)]
	[TestCase("NotARealPolicy", AdminRouteLogPolicy.Protected)]
	[NonParallelizable]
	public void EnvironmentArgsParsesAdminRouteLogPolicy(string rawPolicy, AdminRouteLogPolicy expectedPolicy)
	{
		var previousPolicy = Environment.GetEnvironmentVariable(AdminRouteLogPolicyEnvVar);
		try
		{
			Environment.SetEnvironmentVariable(AdminRouteLogPolicyEnvVar, rawPolicy);
			var args = new EnvironmentArgs();

			Assert.AreEqual(expectedPolicy, args.AdminRouteLogPolicy);
		}
		finally
		{
			Environment.SetEnvironmentVariable(AdminRouteLogPolicyEnvVar, previousPolicy);
		}
	}

	[Test]
	[NonParallelizable]
	public async Task ProtectedAdminRouteDoesNotApplyServiceLogRules()
	{
		var realmConfig = new RecordingRealmConfigService(LogLevel.Debug);
		var loggingContext = new RecordingLoggingContextService();
		TestSocket testSocket = null;

		var ms = new TestSetup(new TestSocketProvider(socket =>
		{
			testSocket = socket;
			socket
				.AddStandardMessageHandlers()
				.AddMessageHandler(
					MessageMatcher
						.WithReqId(1)
						.WithStatus(200)
						.WithPayload<string>(payload => payload == "responsive"),
					MessageResponder.NoResponse(),
					MessageFrequency.OnlyOnce());
		}));

		await ms.Start<LogPolicyService>(new TestArgs
		{
			AdminRouteLogPolicy = AdminRouteLogPolicy.Protected
		}, builder => RegisterLogServices(builder, realmConfig, loggingContext));

		Assert.IsTrue(ms.HasInitialized);
		var updateLogLevelCount = realmConfig.UpdateLogLevelCount;
		var getLogLevelContextCount = loggingContext.GetLogLevelContextCount;

		testSocket.SendToClient(ClientRequest.Callable("micro_log_policy", "admin/HealthCheck", 1));
		await Task.Delay(50);

		Assert.AreEqual(updateLogLevelCount, realmConfig.UpdateLogLevelCount);
		Assert.AreEqual(getLogLevelContextCount, loggingContext.GetLogLevelContextCount);

		await ms.OnShutdown(this, null);
		Assert.IsTrue(testSocket.AllMocksCalled());
	}

