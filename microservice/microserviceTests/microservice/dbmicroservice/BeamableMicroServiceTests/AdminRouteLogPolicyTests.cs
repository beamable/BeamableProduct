	[Test]
	[NonParallelizable]
	public async Task ProtectedPolicyStillAppliesServiceLogRulesForNonAdminRoute()
	{
		var realmConfig = new RecordingRealmConfigService(LogLevel.Debug);
		var loggingContext = new RecordingLoggingContextService();
		TestSocket testSocket = null;

		var ms = new TestSetup(new TestSocketProvider(socket =>
