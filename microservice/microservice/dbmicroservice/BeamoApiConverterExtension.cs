using Beamable.Api.Autogenerated.Models;
using Microsoft.Extensions.Logging;
using System;

namespace microservice.dbmicroservice
{
	public static class BeamoApiConverterExtension
	{
		public static LogLevel ToMicrosoftLogLevel(this BeamoV2LogLevel beamoLogLevel)
		{
			if (Enum.TryParse<LogLevel>(beamoLogLevel.ToString(), out var logLevel))
				return logLevel;
			throw new Exception($"Could not convert type {beamoLogLevel} to {typeof(LogLevel)}");
		} 
	}
}
