using Beamable.Api.Autogenerated.EventPlayers;
using Beamable.Common;
using Beamable.Common.Api;
using Beamable.Common.Api.Events;
using ConverterExtensions = Beamable.Api.Autogenerated.Models.LegacyConverterExtensions;

namespace Beamable.Server.Api.Events
{
   public class MicroserviceEventsApi : AbsEventsApi, IMicroserviceEventsApi
   {
      public MicroserviceEventsApi(IEventPlayersApi eventPlayersApi, IUserContext ctx) : base(eventPlayersApi, ctx)
      {
      }

      public override Promise<EventsGetResponse> GetCurrent(string scope = "")
      { 
	      return EventPlayersApi.ObjectGet(UserContext.UserId).Map(ConverterExtensions.ToLegacy);
      }
   }
}
