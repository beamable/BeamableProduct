using Beamable.Api.Autogenerated.Models;
using Beamable.Api.Autogenerated.Push;
using Beamable.Common;

namespace Beamable.Server.Api.Push
{

	public class MicroservicePushApi : IMicroservicePushApi
	{
		private readonly IPushApi _api;

		public MicroservicePushApi(IPushApi api)
		{
			_api = api;
		}
		
		public async Promise Push(params SendMsg[] messages)
		{
			await _api.PostSend(new SendReq { msgs = messages });
		}
	}

}
