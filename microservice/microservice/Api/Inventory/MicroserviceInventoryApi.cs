using Beamable.Api.Autogenerated.Models;
using System.Collections.Generic;
using Beamable.Common;
using Beamable.Common.Api;
using Beamable.Common.Api.Inventory;
using Beamable.Common.Content;
using System;
using CurrencyProperty = Beamable.Common.Api.Inventory.CurrencyProperty;
using InventoryView = Beamable.Common.Api.Inventory.InventoryView;

namespace Beamable.Server.Api.Inventory
{
	public class MicroserviceInventoryApi : AbsInventoryApi, IMicroserviceInventoryApi
	{
		public MicroserviceInventoryApi(IUserContext userContext,
			Beamable.Api.Autogenerated.Inventory.IInventoryApi inventoryApi) : base(inventoryApi, userContext)
		{
		}

		public override Promise<InventoryView> GetCurrent(string scope = "")
		{
			return InventoryApi.ObjectGet(UserContext.UserId,scope).Map(InventoryViewToAutoGenInventoryView);
		}

		public async Promise SendCurrency(Dictionary<string, long> currencies, long recipientPlayer,
			string transaction = null)
		{
			var transfer = new TransferRequest
			{
				currencies = new OptionalMapOfLong() { HasValue = true, Value = new MapOfLong(currencies) },
				recipientPlayer = recipientPlayer,
				transaction = new OptionalString
				{
					HasValue = !string.IsNullOrWhiteSpace(transaction), Value = transaction
				}
			};
			await InventoryApi.ObjectPutTransfer(UserContext.UserId, transfer);
		}

		
		private InventoryView InventoryViewToAutoGenInventoryView(Beamable.Api.Autogenerated.Models.InventoryView res)
		{
			var inventoryView = new InventoryView();

			// Map currencies and currencies properties
			if (res.currencies != null)
			{
				foreach (var currency in res.currencies)
				{
					inventoryView.currencies[currency.id] = currency.amount;

					if (currency.properties is { Length: > 0 })
					{
						var propertyList = new List<CurrencyProperty>();
						foreach (var prop in currency.properties)
						{
							propertyList.Add(new CurrencyProperty { name = prop.name, value = prop.value });
						}

						inventoryView.currencyProperties[currency.id] = propertyList;
					}
				}
			}

			// Map items
			if (res.items != null)
			{
				foreach (var itemGroup in res.items)
				{
					var itemViewList = new List<ItemView>();

					if (itemGroup.items != null)
					{
						foreach (var item in itemGroup.items)
						{
							var itemView = new ItemView
							{
								id = item.id,
								contentId = itemGroup.id,
								createdAt = item.createdAt.HasValue ? item.createdAt.Value : 0,
								updatedAt = item.updatedAt.HasValue ? item.updatedAt.Value : 0,
								properties = new Dictionary<string, string>()
							};

							if (item.properties != null)
							{
								foreach (var prop in item.properties)
								{
									itemView.properties[prop.name] = prop.value;
								}
							}

							itemViewList.Add(itemView);
						}
					}

					inventoryView.items[itemGroup.id] = itemViewList;
				}
			}
			
			// Map item filters
			if (res.itemFilters is { HasValue: true })
			{
				var filterView = new InventoryFilterView();
				var filters = res.itemFilters.Value;
				
				if (filters.contentTagFilter is { HasValue: true })
				{
					filterView.contentTagFilter = new ContentTagFilterView
					{
						allOf = filters.contentTagFilter.Value.allOf,
						anyOf = filters.contentTagFilter.Value.anyOf,
						noneOf = filters.contentTagFilter.Value.noneOf
					};
				}
				
				if (filters.createdAt is { HasValue: true })
				{
					filterView.createdAt = new TimeRangeView
					{
						from = filters.createdAt.Value.from.GetOrElse(0),
						to = filters.createdAt.Value.to.GetOrElse(0)
					};
				}
				
				
				var propertyFiltersList = new List<PropertyFilterView>();
				if (filters.propertyFilters is { HasValue: true })
				{
					foreach (var filter in filters.propertyFilters.Value)
					{
						propertyFiltersList.Add(new PropertyFilterView
						{
							key = filter.key,
							predicate = new PredicateView
							{
								asKey = filter.predicate.asKey.GetOrElse(string.Empty),
								caseInsensitive = filter.predicate.caseInsensitive.GetOrElse(false),
								includeLower = filter.predicate.includeLower.GetOrElse(false),
								includeUpper = filter.predicate.includeUpper.GetOrElse(false),
								kind = filter.predicate.kind,
								lower = filter.predicate.lower.GetOrElse(string.Empty),
								substr = filter.predicate.substr.GetOrElse(string.Empty),
								upper = filter.predicate.upper.GetOrElse(string.Empty),
								value = filter.predicate.value.GetOrElse(string.Empty),
								values = filter.predicate.values.GetOrElse(Array.Empty<string>())
							}
						});
					}
				}

				filterView.propertyFilters = propertyFiltersList.ToArray();
				
				
				if (filters.proxyIds is { HasValue: true })
				{
					filterView.proxyIds = filters.proxyIds.Value;
				}
				
				if (filters.updatedAt is { HasValue: true })
				{
					filterView.updatedAt = new TimeRangeView
					{
						from = filters.updatedAt.Value.from.HasValue ? filters.updatedAt.Value.from.Value : 0,
						to = filters.updatedAt.Value.to.HasValue ? filters.updatedAt.Value.to.Value : 0
					};
				}
				
				inventoryView.itemFilters = filterView;
			}
			
			
			
			inventoryView.scope = res.scope.GetOrElse(string.Empty);

			return inventoryView;
		}
	}
}
