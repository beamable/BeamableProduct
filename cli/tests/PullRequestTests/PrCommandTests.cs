using Beamable.Api.Autogenerated.Beamopullrequest;
using Beamable.Api.Autogenerated.Models;
using Beamable.Common.Content;
using Moq;
using NUnit.Framework;
using System;
using tests.Examples;
using tests.MoqExtensions;

namespace tests.PullRequestTests;

[NonParallelizable]
public class PrCommandTests : CLITestExtensions
{
	private void InitWorkspace()
	{
		SetupMocks(mockBeamoManifest: false, mockAdminMe: false);
		Ansi.Input.PushTextWithEnter(alias);
		Ansi.Input.PushTextWithEnter(userName);
		Ansi.Input.PushTextWithEnter(password);
		Ansi.Input.PushKey(ConsoleKey.Enter);
		Ansi.Input.PushKey(ConsoleKey.Enter);

		Run("init", "--save-to-file");
		_mockObjects.Clear();
		ResetConfigurator();
	}

	private void MockPrApi(Action<Mock<IBeamBeamopullrequestApi>> configure)
	{
		Mock<IBeamBeamopullrequestApi>(configure);
	}

	#region submit

	[Test]
	public void Submit_CallsApi()
	{
		InitWorkspace();
		MockPrApi(mock =>
		{
			mock.Setup(x => x.PostPrs(It.IsAny<SubmitPullRequestRequest>(), It.IsAny<bool>()))
				.ReturnsPromise(new SubmitPullRequestResponse { id = "pr-1" })
				.Verifiable();
		});

		Run("org", "pr", "submit", "--source-realm", "source-pid", "--quiet");
	}

	[Test]
	public void Submit_Fails_WhenNoSource()
	{
		InitWorkspace();
		var exitCode = RunFull(new[] { "org", "pr", "submit", "--quiet" });
		Assert.That(exitCode, Is.EqualTo(1), "submit without --source-realm must fail");
	}

	#endregion

	#region list

	[Test]
	public void List_CallsApi()
	{
		InitWorkspace();
		MockPrApi(mock =>
		{
			mock.Setup(x => x.GetPrs(It.IsAny<Optional<int>>(), It.IsAny<Optional<int>>(),
					It.IsAny<Optional<PullRequestStatus>>(), It.IsAny<bool>()))
				.ReturnsPromise(new ListPullRequestsResponse())
				.Verifiable();
		});

		Run("org", "pr", "list", "--quiet");
	}

	[Test]
	public void List_Fails_WhenInvalidStatus()
	{
		InitWorkspace();
		var exitCode = RunFull(new[] { "org", "pr", "list", "--status", "NotAStatus", "--quiet" });
		Assert.That(exitCode, Is.EqualTo(1), "list with an invalid --status must fail");
	}

	#endregion

	#region diff

	[Test]
	public void Diff_Fails_WhenNoId()
	{
		InitWorkspace();
		var exitCode = RunFull(new[] { "org", "pr", "diff", "--quiet" });
		Assert.That(exitCode, Is.EqualTo(1), "diff without --id must fail");
	}

	[Test]
	public void Diff_CallsApi()
	{
		InitWorkspace();
		MockPrApi(mock =>
		{
			mock.Setup(x => x.GetPrs("pr-1", It.IsAny<bool>()))
				.ReturnsPromise(new GetPullRequestResponse())
				.Verifiable();
		});

		Run("org", "pr", "diff", "--id", "pr-1", "--quiet");
	}

	#endregion

	#region comment

	[Test]
	public void Comment_Fails_WhenNoId()
	{
		InitWorkspace();
		var exitCode = RunFull(new[] { "org", "pr", "comment", "-m", "hi", "--quiet" });
		Assert.That(exitCode, Is.EqualTo(1), "comment without --id must fail");
	}

	[Test]
	public void Comment_Fails_WhenEmptyMessage()
	{
		InitWorkspace();
		var exitCode = RunFull(new[] { "org", "pr", "comment", "--id", "pr-1", "-m", "", "--quiet" });
		Assert.That(exitCode, Is.EqualTo(1), "comment with an empty message must fail");
	}

	[Test]
	public void Comment_CallsApi()
	{
		InitWorkspace();
		MockPrApi(mock =>
		{
			mock.Setup(x => x.PostPrsComments("pr-1", It.IsAny<CommentPullRequestRequest>(), It.IsAny<bool>()))
				.ReturnsPromise(new EmptyMessage())
				.Verifiable();
		});

		Run("org", "pr", "comment", "--id", "pr-1", "-m", "hello", "--quiet");
	}

	#endregion

	#region approve / reject

	[Test]
	public void Approve_Quiet_ShowsDiffThenApproves()
	{
		InitWorkspace();
		MockPrApi(mock =>
		{
			mock.Setup(x => x.GetPrs("pr-1", It.IsAny<bool>()))
				.ReturnsPromise(new GetPullRequestResponse())
				.Verifiable();
			mock.Setup(x => x.PostPrsApprove("pr-1", It.IsAny<bool>()))
				.ReturnsPromise(new BeamoPullRequestActorManifestChecksum { id = "pr-1", checksum = "abc" })
				.Verifiable();
		});

		Run("org", "pr", "approve", "--id", "pr-1", "--quiet");
	}

	[Test]
	public void Approve_Fails_WhenNoId()
	{
		InitWorkspace();
		var exitCode = RunFull(new[] { "org", "pr", "approve", "--quiet" });
		Assert.That(exitCode, Is.EqualTo(1), "approve without --id must fail");
	}

	[Test]
	public void Reject_CallsApi()
	{
		InitWorkspace();
		MockPrApi(mock =>
		{
			mock.Setup(x => x.PostPrsReject("pr-1", It.IsAny<bool>()))
				.ReturnsPromise(new EmptyMessage())
				.Verifiable();
		});

		Run("org", "pr", "reject", "--id", "pr-1", "--quiet");
	}

	[Test]
	public void Reject_Fails_WhenNoId()
	{
		InitWorkspace();
		var exitCode = RunFull(new[] { "org", "pr", "reject", "--quiet" });
		Assert.That(exitCode, Is.EqualTo(1), "reject without --id must fail");
	}

	#endregion
}
