using Beamable.Api.Autogenerated.Beamobundle;
using Beamable.Api.Autogenerated.Models;
using Beamable.Server;
using cli.Services.Bundles;
using System.CommandLine;
using System.Linq;

namespace cli.BundleCommands;

public class BundleAclCommandArgs : CommandArgs
{
	public string bundleRef;
	public string scope;
}

public class BundleAclCommandOutput
{
	public string name;
	public string checksum;
	public string scope;
}

/// <summary>
/// Widen a bundle checksum's ACL. Admin-gated server-side.
/// NOTE: the generated client (<c>BeamBeamobundle.gs.cs</c>) issues an HTTP GET with a request body
/// to the <c>/acl</c> endpoint. If the backend expects PATCH this will fail at runtime — verify
/// against the host/OpenAPI source before relying on it (likely a generator/spec mismatch to fix).
/// </summary>
public class BundleAclCommand : AtomicCommand<BundleAclCommandArgs, BundleAclCommandOutput>, ISkipManifest
{
	public BundleAclCommand() : base("acl", "Widen the ACL scope on a bundle checksum")
	{
	}

	public override void Configure()
	{
		AddArgument(new Argument<string>("bundle-ref", "The bundle reference: <bundle-name>, <bundle-name>@<tag>, or <bundle-name>@sha256:<checksum> (a name or tag resolves to its checksum; a bare name means @latest)"),
			(args, i) => args.bundleRef = i);
		AddOption(new Option<string>("--scope", "Visibility tier to widen to (each tier is a superset of the previous, not a list of realms): 'realm' = only this realm; 'org' = every realm in your customer; 'public' = every realm in every customer. A literal <cid>.<pid> / <cid> / * is also accepted"),
			(args, i) => args.scope = i);
	}

	public override async Task<BundleAclCommandOutput> GetResult(BundleAclCommandArgs args)
	{
		var api = args.Provider.GetService<IBeamBeamobundleApi>();
		var (name, selector) = BundleRef.Split(args.bundleRef);
		var ns = await BundleNamespace.Get(args);

		var checksum = selector;
		if (string.IsNullOrEmpty(selector) || !selector.StartsWith("sha256:"))
		{
			// a bare name or a tag selector — resolve the tag to its checksum via the tag map.
			var tag = string.IsNullOrEmpty(selector) ? "latest" : selector;
			var response = await api.GetBundles(name, ns);
			var match = (response.tags ?? System.Array.Empty<BundleTag>()).FirstOrDefault(t => t.tag == tag);
			if (match == null)
				throw new CliException($"Bundle [{name}] has no tag [{tag}]. Use <bundle-name>@sha256:<checksum> for an exact checksum.");
			checksum = match.checksum;
		}

		var scope = BundleAclScope.Resolve(args.scope, args.AppContext);
		await api.PatchBundlesChecksumsAcl(name, checksum, ns, new UpdateBundleAclRequest { scope = scope });
		return new BundleAclCommandOutput { name = BundleNamespace.Qualify(ns, name), checksum = checksum, scope = scope };
	}
}
