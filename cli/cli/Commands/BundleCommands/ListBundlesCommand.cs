using Beamable.Api.Autogenerated.Beamobundle;
using Beamable.Api.Autogenerated.Models;
using Beamable.Common.Api;
using Beamable.Server;
using cli.Services;
using cli.Services.Bundles;
using System;
using System.Collections.Generic;
using System.Linq;

namespace cli.BundleCommands;

public class ListBundlesCommandArgs : CommandArgs
{
}

public class ListBundlesCommandOutput
{
	/// <summary>Published bundles in the catalog that are visible to the current realm.</summary>
	public BundleInfo[] published = Array.Empty<BundleInfo>();

	/// <summary>
	/// Bundles authored in this workspace (may not be published to the catalog yet). Same shape as
	/// <see cref="published"/>; catalog-only fields (checksum, acl, yanked, publishedAt) are empty.
	/// </summary>
	public BundleInfo[] local = Array.Empty<BundleInfo>();
}

public class ListBundlesCommand : AtomicCommand<ListBundlesCommandArgs, ListBundlesCommandOutput>
{
	public ListBundlesCommand() : base("list", "List locally authored bundles and published bundles visible to the current realm")
	{
	}

	public override void Configure()
	{
	}

	public override async Task<ListBundlesCommandOutput> GetResult(ListBundlesCommandArgs args)
	{
		// The logical bundle name is always @<alias>/<bundle-name>, same as the published entries.
		// Resolved best-effort so the local list still works when the namespace can't be resolved.
		var ns = await BundleNamespace.TryGet(args);
		if (ns == null)
			Log.Warning("Could not resolve the bundle namespace (customer alias); showing local bundle names without it.");

		// Locally-authored bundles from the workspace (use Discover, not DiscoverAndValidate, so a
		// partition conflict doesn't prevent listing).
		var local = BundleWorkspace.Discover(args.ConfigService)
			.Select(b => FromLocalBundle(b, args.BeamoLocalSystem.BeamoManifest, ns))
			.ToArray();

		// Published catalog bundles. Tolerate a fetch failure (e.g. not logged in / no realm) so the
		// local list is still returned.
		BundleInfo[] published;
		try
		{
			var response = await args.Provider.GetService<IBeamBeamobundleApi>().GetBundles();
			published = (response.bundles ?? Array.Empty<Bundle>()).Select(BundleInfo.FromBundle).ToArray();
		}
		catch (RequesterException e)
		{
			Log.Warning($"Could not fetch published bundles from the catalog (status={e.Status}); showing local bundles only.");
			published = Array.Empty<BundleInfo>();
		}

		return new ListBundlesCommandOutput { published = published, local = local };
	}

	/// <summary>
	/// Project an authored bundle config into the same shape as a published catalog entry. The
	/// authored config only lists flat beamoIds, so they are classified into services/storages/portal
	/// extensions using the local manifest.
	/// </summary>
	private static BundleInfo FromLocalBundle(BundleConfigFile bundle, BeamoLocalManifest localManifest, string ns)
	{
		var services = new List<string>();
		var storages = new List<string>();
		var extensions = new List<string>();
		foreach (var component in bundle.components)
		{
			var definition = localManifest.ServiceDefinitions.FirstOrDefault(d => d.BeamoId == component);
			switch (definition?.Protocol)
			{
				case BeamoProtocolType.HttpMicroservice:
					services.Add(component);
					break;
				case BeamoProtocolType.EmbeddedMongoDb:
					storages.Add(component);
					break;
				case BeamoProtocolType.PortalExtension:
					extensions.Add(component);
					break;
				default:
					Log.Warning($"Bundle=[{bundle.name}] lists component=[{component}] which is not a known local service/storage/extension.");
					break;
			}
		}

		return new BundleInfo
		{
			name = ns == null ? bundle.name : BundleNamespace.Qualify(ns, bundle.name),
			filePath = bundle.filePath,
			serviceNames = services.ToArray(),
			storageIds = storages.ToArray(),
			portalExtensionNames = extensions.ToArray(),
			peerDependencies = bundle.peerDependencies.ToDictionary(kvp => kvp.Key, kvp => kvp.Value?.type ?? ""),
		};
	}
}
