using System;
using System.Collections.Generic;
using System.Linq;
using Beamable.Api.Autogenerated.Models;

namespace cli.BundleCommands;

/// <summary>
/// CLI-owned projection of a catalog <see cref="Bundle"/> for command output. The generated
/// <see cref="Bundle"/> transitively exposes runtime serialization types (e.g. MapOfString) that
/// don't live in the CLI assembly; the CLI output contract must be defined here instead so that
/// changes to the generated shape can't silently become breaking changes. Only the scalar summary
/// fields are surfaced — the full component refs stay server-side.
/// </summary>
[Serializable]
public class BundleInfo
{
	public string name;

	/// <summary>
	/// Content checksum. Empty for a local bundle that hasn't been published: the checksum is
	/// content-addressed and computed by the server at publish time (over the built component
	/// checksums), so it isn't known from the authored config alone.
	/// </summary>
	public string checksum = "";

	public string acl = "";
	public bool yanked;
	public long publishedAt;

	/// <summary>Absolute path of the authored config file. Empty for a published catalog bundle.</summary>
	public string filePath = "";

	public string[] serviceNames = Array.Empty<string>();
	public string[] storageIds = Array.Empty<string>();
	public string[] portalExtensionNames = Array.Empty<string>();
	public Dictionary<string, string> peerDependencies = new Dictionary<string, string>();

	public static BundleInfo FromBundle(Bundle bundle)
	{
		if (bundle == null) return null;

		var peers = new Dictionary<string, string>();
		var peerMap = bundle.peerDependencies.GetOrElse(new MapOfBundlePeerDep());
		foreach (var kvp in peerMap)
		{
			peers[kvp.Key] = kvp.Value?.type.GetOrElse("");
		}

		return new BundleInfo
		{
			name = bundle.name.GetOrElse(""),
			checksum = bundle.checksum.GetOrElse(""),
			acl = bundle.acl.GetOrElse(""),
			yanked = bundle.yanked.GetOrElse(false),
			publishedAt = bundle.publishedAt.GetOrElse(0),
			serviceNames = bundle.serviceReferences.GetOrElse(Array.Empty<ServiceReference>())
				.Select(s => s.serviceName.GetOrElse("")).ToArray(),
			storageIds = bundle.storageReferences.GetOrElse(Array.Empty<ServiceStorageReference>())
				.Select(s => s.id.GetOrElse("")).ToArray(),
			portalExtensionNames = bundle.portalExtensionReferences.GetOrElse(Array.Empty<PortalExtensionReference>())
				.Select(p => (string)p.name).ToArray(),
			peerDependencies = peers,
		};
	}
}
