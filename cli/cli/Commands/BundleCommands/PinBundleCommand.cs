using Beamable.Api.Autogenerated.Beamobundle;
using Beamable.Server;
using cli.Services.Bundles;
using System.CommandLine;
using System.Linq;

namespace cli.BundleCommands;

public class PinBundleCommandArgs : CommandArgs
{
	public string bundleName;
	public string tag;
	public string checksum;
}

public class PinBundleCommandOutput
{
	public string name;
	public string checksum;
}

public class PinBundleCommand : AtomicCommand<PinBundleCommandArgs, PinBundleCommandOutput>, ISkipManifest
{
	public PinBundleCommand() : base("pin", "Pin a bundle checksum into the local manifest.beam.json references")
	{
	}

	public override void Configure()
	{
		AddArgument(new Argument<string>("bundle-name", "The bundle name"),
			(args, i) => args.bundleName = i);
		AddOption(new Option<string>("--tag", () => null, "Resolve the checksum from this tag (defaults to latest)"),
			(args, i) => args.tag = i);
		AddOption(new Option<string>("--checksum", () => null, "Pin this exact checksum (sha256:<checksum>) instead of a tag"),
			(args, i) => args.checksum = i);
	}

	public override async Task<PinBundleCommandOutput> GetResult(PinBundleCommandArgs args)
	{
		BundleWorkspace.ValidateName(args.bundleName);
		var ns = await BundleNamespace.Get(args);
		var fullName = BundleNamespace.Qualify(ns, args.bundleName);

		var checksum = args.checksum;
		if (string.IsNullOrEmpty(checksum))
		{
			var tag = string.IsNullOrEmpty(args.tag) ? "latest" : args.tag;
			var api = args.Provider.GetService<IBeamBeamobundleApi>();
			var response = await api.GetBundles(args.bundleName, ns);
			var match = (response.tags ?? System.Array.Empty<Beamable.Api.Autogenerated.Models.BundleTag>())
				.FirstOrDefault(t => t.tag == tag);
			if (match == null)
				throw new CliException($"Bundle [{args.bundleName}] has no tag [{tag}]. Use --checksum to pin an exact checksum.");
			checksum = match.checksum;
		}

		var manifest = args.ConfigService.LoadManifestReferences() ?? new ManifestReferences();
		manifest.references[fullName] = checksum;
		args.ConfigService.SaveManifestReferences(manifest);

		Log.Information($"Pinned [{fullName}] → [{checksum}] in {ConfigService.MANIFEST_FILE_NAME}");
		return new PinBundleCommandOutput { name = fullName, checksum = checksum };
	}
}
