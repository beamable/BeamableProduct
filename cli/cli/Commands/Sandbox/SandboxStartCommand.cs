using System.CommandLine;
using System.Threading;
using Beamable.Api.Autogenerated.Accounts;
using Beamable.Common.Dependencies;
using Beamable.Server;
using cli.Services;
using cli.Services.Sandbox;
using cli.Utils;
using Spectre.Console;

namespace cli.Sandbox;

public class SandboxStartCommandArgs : CommandArgs
{
	public string? Name;
	public bool Open;
	public string? JoinCode;
}

public class SandboxStartCommandResult
{
	public string step = string.Empty;
	public string message = string.Empty;
	public float progressRatio;
	public string? serviceName;
	public string? joinCode;
	public string? pairLink;
	public string? repoRoot;
}

public class SandboxStartCommand : StreamCommand<SandboxStartCommandArgs, SandboxStartCommandResult>
{
	public SandboxStartCommand() : base("start", "Start a sandbox bound to this repo, exposing it to Portal for authoring")
	{
	}

	public override void Configure()
	{
		AddOption(new Option<string?>("--name", () => null, "Optional human-readable label shown in Portal listings"),
			(args, v) => args.Name = v);
		AddOption(new Option<bool>("--open", () => false, "Open the Portal pair link in the default browser"),
			(args, v) => args.Open = v);
		AddOption(new Option<string?>("--join-code", () => null,
				"Use this exact join code instead of generating one. Intended for the Portal's `generate then run this` pair flow — Portal mints the code, shows the command, and waits for the sandbox to come up with the matching code. Must match the GenerateJoinCode format (`XXXX-XXXX`, alphabet 2-9A-HJKMNP-Z)"),
			(args, v) => args.JoinCode = v);
	}

	public override async Task Handle(SandboxStartCommandArgs args)
	{
		var repoRoot = ResolveRepoRoot(args);

		SendResults(new SandboxStartCommandResult
		{
			step = "resolved-repo",
			message = $"Repo root: {repoRoot}",
			progressRatio = 0.10f,
			repoRoot = repoRoot,
		});

		var accountId = await ResolveAccountId(args);

		var serviceName = SandboxNaming.BuildServiceName(accountId);
		string joinCode;
		if (!string.IsNullOrEmpty(args.JoinCode))
		{
			if (!SandboxNaming.IsValidJoinCodeFormat(args.JoinCode))
			{
				throw new CliException(
					$"Invalid --join-code '{args.JoinCode}'. Expected format XXXX-XXXX using the alphabet 2-9A-HJKMNP-Z (no 0/O/1/I/L).");
			}
			joinCode = args.JoinCode.ToUpperInvariant();
		}
		else
		{
			joinCode = SandboxNaming.GenerateJoinCode();
		}

		SendResults(new SandboxStartCommandResult
		{
			step = "minted-credentials",
			message = $"Allocated sandbox {serviceName}",
			progressRatio = 0.40f,
			serviceName = serviceName,
		});

		var state = new SandboxStateService();
		state.WriteCode(serviceName, joinCode);

		var pairLink = BuildPairLink(args, serviceName, joinCode);
		var sandboxVersion = args.AppContext.ExecutingVersion;

		var observer = new SandboxObserver(
			launcherAccountId: accountId,
			cid: args.AppContext.Cid,
			pid: args.AppContext.Pid,
			serviceName: serviceName,
			repoRoot: repoRoot,
			joinCode: joinCode,
			sandboxVersion: sandboxVersion,
			label: args.Name,
			clock: new SystemClock());

		// Build the CLI schema once at startup so Portal can fetch it cheap
		// over the GetSchema route. Walks reflection on the command tree —
		// the only place it's expensive enough to be worth caching.
		try
		{
			var cliContext = args.DependencyProvider.GetService<CliGenerator>().GetCliContext();
			observer.BindSchema(CliSchemaGeneratorCommand.BuildSchema(cliContext));
		}
		catch (System.Exception ex)
		{
			SandboxLog.Warn($"[sandbox] failed to build CLI schema at startup: {ex.Message}");
		}

		// Bind the subprocess runner used by Invoke(outOfProcess: true) callers.
		// Re-execs the same beam binary the sandbox is running as; lets commands
		// that bind HTTP listeners (project run, etc.) avoid colliding with the
		// sandbox's own listener.
		var subprocessRunner = new SubprocessInvocationRunner(
			executablePath: System.Environment.ProcessPath ?? "beam",
			workingDirectory: repoRoot,
			hostProcessId: System.Environment.ProcessId);
		observer.BindOutOfProcessRunner(subprocessRunner);

		SendResults(new SandboxStartCommandResult
		{
			step = "ready-to-pair",
			message = "Sandbox is up; pair from Portal to begin authoring (Ctrl-C to stop)",
			progressRatio = 1.0f,
			serviceName = serviceName,
			joinCode = joinCode,
			pairLink = pairLink,
			repoRoot = repoRoot,
		});

		AnsiConsole.MarkupLine($"\n[bold]Sandbox ready:[/] [yellow]{serviceName.EscapeMarkup()}[/]");
		AnsiConsole.MarkupLine($"[bold]Join code:[/] [green]{joinCode.EscapeMarkup()}[/]");
		AnsiConsole.MarkupLine($"[bold]Pair link:[/] {pairLink.EscapeMarkup()}");
		AnsiConsole.MarkupLine("[grey]Code stored locally — recover via [bold]beam sandbox ps[/] if you scroll past it.[/]");

		if (args.Open)
		{
			MachineHelper.OpenBrowser(pairLink);
		}

		using var ctrlCSource = new CancellationTokenSource();
		Console.CancelKeyPress += (_, e) =>
		{
			e.Cancel = true;
			// Emit a goodbye event + flush the batcher synchronously so the Portal
			// receives a shutdown-imminent frame before the gateway tears down our
			// notifications connection. Without this, the Portal only finds out
			// the sandbox is gone via a 503 on the next RPC.
			try { observer.SignalShutdown(ShutdownReason.HostTerminating); }
			catch (Exception ex) { SandboxLog.Warn($"[sandbox] SignalShutdown threw on Ctrl-C: {ex.Message}"); }
			ctrlCSource.Cancel();
		};

		try
		{
			var beamoLocalSystem = args.DependencyProvider.GetService<BeamoLocalSystem>();
			var runner = new RealInvocationRunner();
			await beamoLocalSystem.RunLocalSandbox(observer, runner, ctrlCSource.Token);
		}
		finally
		{
			state.RemoveCode(serviceName);
		}

		AnsiConsole.MarkupLine("[grey]Sandbox stopped.[/]");
	}

	private static string ResolveRepoRoot(SandboxStartCommandArgs args)
	{
		// ConfigService walked up from the working dir during its construction and either
		// found a .beamable folder or didn't. Reuse that result rather than re-implementing
		// the walk — it also keeps the "what is the workspace root" semantics aligned with
		// every other command in the CLI.
		var config = args.ConfigService;
		if (config.DirectoryExists != true)
		{
			throw new CliException(
				"Sandbox must be started from a Beamable workspace (no .beamable folder found in the working directory or any ancestor). Run `beam init` first.");
		}
		return config.BeamableWorkspace;
	}

	private static async Task<long> ResolveAccountId(CommandArgs args)
	{
		// The launcher account id is captured here so the MS can compare it to RequestContext.AccountId
		// on every authenticated route. We pull it from the same /accounts/admin-me path the rest
		// of the CLI uses for current-user resolution.
		try
		{
			var accounts = args.DependencyProvider.GetService<IAccountsApi>();
			var me = await accounts.GetAdminMe(true);
			return me.id;
		}
		catch (Exception ex)
		{
			Log.Warning($"Could not resolve account id from /accounts/admin-me; using a temporary placeholder. {ex.Message}");
			return 0;
		}
	}

	private static string BuildPairLink(CommandArgs args, string serviceName, string joinCode)
	{
		// Pair page lives under the standard realm route. By Beamable convention (see
		// cli.Portal.PortalCommand.GetPortalRealmUrl) gameId = pid in the URL, so the
		// CLI can build the deep link with only cid + pid and no extra realm-API
		// lookup. svc + code stay in the query string.
		var portalUrl = cli.Portal.PortalCommand.GetPortalBaseUrl(args);
		var cid = args.AppContext.Cid;
		var pid = args.AppContext.Pid;
		return $"{portalUrl}/{Uri.EscapeDataString(cid)}/games/{Uri.EscapeDataString(pid)}/realms/{Uri.EscapeDataString(pid)}/sandbox/pair?svc={Uri.EscapeDataString(serviceName)}&code={Uri.EscapeDataString(joinCode)}";
	}
}
