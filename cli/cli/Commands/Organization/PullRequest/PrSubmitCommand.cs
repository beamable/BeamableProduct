using Beamable.Api.Autogenerated.Beamopullrequest;
using Beamable.Api.Autogenerated.Models;
using Beamable.Server;
using System.CommandLine;

namespace cli;

public class PrSubmitCommandArgs : CommandArgs
{
	public string SourceRealm;
	public string Comment;
}

public class PrSubmitCommandOutput
{
	/// <summary>The id of the created pull request.</summary>
	public string id;
}

public class PrSubmitCommand : AtomicCommand<PrSubmitCommandArgs, PrSubmitCommandOutput>, ISkipManifest
{
	public PrSubmitCommand() : base("submit", "Submit a pull request proposing a new manifest for the current realm")
	{
	}

	public override void Configure()
	{
		AddOption(new Option<string>("--source-realm", "Snapshot this realm's current manifest as the proposal"),
			(args, i) => args.SourceRealm = i);
		AddOption(new Option<string>(new[] { "--comment", "-c" }, "An optional comment to attach to the pull request"),
			(args, i) => args.Comment = i);
	}

	public override async Task<PrSubmitCommandOutput> GetResult(PrSubmitCommandArgs args)
	{
		if (string.IsNullOrEmpty(args.SourceRealm))
		{
			throw new CliException("--source-realm is required. Use --source-realm <pid> to specify the realm whose manifest to propose.");
		}

		// The target scope is the current realm (resolved from the request scope header). Use the
		// standard global --cid/--pid overrides to target a different realm.
		var request = new SubmitPullRequestRequest { sourceScopeId = args.SourceRealm };
		if (!string.IsNullOrEmpty(args.Comment))
		{
			request.comment = args.Comment;
		}

		var api = args.Provider.GetService<IBeamBeamopullrequestApi>();
		var response = await api.PostPrs(request);

		return new PrSubmitCommandOutput { id = response.id.GetOrElse((string)null) };
	}
}
