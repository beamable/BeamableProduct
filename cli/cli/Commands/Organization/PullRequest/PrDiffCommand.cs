using Beamable.Api.Autogenerated.Beamopullrequest;
using Beamable.Api.Autogenerated.Models;
using Beamable.Server;
using System.CommandLine;

namespace cli;

public class PrDiffCommandArgs : CommandArgs
{
	public string Id;
}

public class PrDiffCommandOutput
{
	public PrSummary pullRequest;
	public ManifestDiffResponse diff;
}

public class PrDiffCommand : AtomicCommand<PrDiffCommandArgs, PrDiffCommandOutput>, ISkipManifest
{
	public PrDiffCommand() : base("diff", "Show a pull request with its diff against the current manifest")
	{
	}

	public override void Configure()
	{
		AddOption(new Option<string>("--id", "The id of the pull request to inspect"),
			(args, i) => args.Id = i);
	}

	public override async Task<PrDiffCommandOutput> GetResult(PrDiffCommandArgs args)
	{
		if (string.IsNullOrEmpty(args.Id))
		{
			throw new CliException("--id is required. Use --id <pullRequestId> to specify the pull request to inspect.");
		}

		var api = args.Provider.GetService<IBeamBeamopullrequestApi>();
		var response = await api.GetPrs(args.Id);

		var pr = response.pullRequest.HasValue ? response.pullRequest.Value : null;
		var diff = response.diff.HasValue ? response.diff.Value : null;

		PrCommand.PrintInfo(pr);
		PrCommand.PrintDiff(diff);

		return new PrDiffCommandOutput
		{
			pullRequest = pr == null ? null : PrSummary.From(pr),
			diff = diff,
		};
	}
}
