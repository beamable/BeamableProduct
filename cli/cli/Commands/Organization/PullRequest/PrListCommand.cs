using Beamable.Api.Autogenerated.Beamopullrequest;
using Beamable.Api.Autogenerated.Models;
using Beamable.Common.Content;
using Beamable.Server;
using System;
using System.Linq;
using System.CommandLine;

namespace cli;

/// <summary>A flattened, human-friendly view of a <see cref="ProposedManifest"/> pull request.</summary>
public class PrSummary
{
	public string id;
	public string status;
	public string sourceScopeId;
	public long submittedByAccountId;
	public long created;
	public long resolvedAt;
	public long resolvedByAccountId;
	public string resultManifestId;
	public string lastApplyError;

	public static PrSummary From(ProposedManifest pr)
	{
		return new PrSummary
		{
			id = pr.id.GetOrElse((string)null),
			status = pr.status.HasValue ? pr.status.Value.ToString() : null,
			sourceScopeId = pr.sourceScopeId.GetOrElse((string)null),
			submittedByAccountId = pr.submittedByAccountId.GetOrElse(0),
			created = pr.created.GetOrElse(0),
			resolvedAt = pr.resolvedAt.GetOrElse(0),
			resolvedByAccountId = pr.resolvedByAccountId.GetOrElse(0),
			resultManifestId = pr.resultManifestId.GetOrElse((string)null),
			lastApplyError = pr.lastApplyError.GetOrElse((string)null),
		};
	}
}

public class PrListCommandArgs : CommandArgs
{
	public string Status;
	public int? Limit;
	public int? Offset;
}

public class PrListCommandOutput
{
	public PrSummary[] pullRequests = Array.Empty<PrSummary>();
}

public class PrListCommand : AtomicCommand<PrListCommandArgs, PrListCommandOutput>, ISkipManifest
{
	public PrListCommand() : base("list", "List pull requests targeting the current realm")
	{
	}

	public override void Configure()
	{
		AddOption(new Option<string>("--status", "Filter by status: Pending, Approved, Rejected, or Superseded"),
			(args, i) => args.Status = i);
		AddOption(new Option<int?>("--limit", "Maximum number of pull requests to return"),
			(args, i) => args.Limit = i);
		AddOption(new Option<int?>("--offset", "Number of pull requests to skip"),
			(args, i) => args.Offset = i);
	}

	public override async Task<PrListCommandOutput> GetResult(PrListCommandArgs args)
	{
		var status = new OptionalPullRequestStatus();
		if (!string.IsNullOrEmpty(args.Status))
		{
			if (!Enum.TryParse<PullRequestStatus>(args.Status, ignoreCase: true, out var parsed))
			{
				throw new CliException("Invalid --status. Allowed values: Pending, Approved, Rejected, Superseded.");
			}
			status = new OptionalPullRequestStatus(parsed);
		}

		var limit = args.Limit.HasValue ? new OptionalInt(args.Limit.Value) : new OptionalInt();
		var offset = args.Offset.HasValue ? new OptionalInt(args.Offset.Value) : new OptionalInt();

		var api = args.Provider.GetService<IBeamBeamopullrequestApi>();
		var response = await api.GetPrs(limit, offset, status);

		var pullRequests = (response.pullRequests.HasValue ? response.pullRequests.Value : null) ?? Array.Empty<ProposedManifest>();
		return new PrListCommandOutput { pullRequests = pullRequests.Select(PrSummary.From).ToArray() };
	}
}
