using Beamable.Api.Autogenerated.Models;
using Beamable.Server;
using System.Linq;
using System.Text;

namespace cli;

public class PrCommand : CommandGroup
{
	public PrCommand() : base("pr", "Commands for manifest pull requests")
	{
	}

	/// <summary>
	/// Render a server-computed manifest diff (component and bundle-reference changes) to the user.
	/// Shared by the <c>diff</c> and <c>approve</c> commands. Modeled on <c>BundleDiff.Print</c>.
	/// </summary>
	public static void PrintDiff(ManifestDiffResponse diff)
	{
		if (diff == null)
		{
			Log.Information("No diff available for this pull request.");
			return;
		}

		var components = (diff.components.HasValue ? diff.components.Value : null) ?? System.Array.Empty<ManifestDiffEntry>();
		var references = (diff.references.HasValue ? diff.references.Value : null) ?? System.Array.Empty<ReferenceDiffEntry>();

		if (components.Length == 0 && references.Length == 0)
		{
			Log.Information("This pull request makes no changes to the current manifest.");
			return;
		}

		PrintComponentChanges(components, ManifestDiffChange.Added, "Adding");
		PrintComponentChanges(components, ManifestDiffChange.Changed, "Changing");
		PrintComponentChanges(components, ManifestDiffChange.Removed, "Removing");

		if (references.Length > 0)
		{
			var sb = new StringBuilder();
			sb.AppendLine(references.Length == 1 ? "1 bundle reference change" : $"{references.Length} bundle reference changes");
			foreach (var reference in references)
			{
				var bundleName = reference.bundleName.GetOrElse("<unknown>");
				var oldChecksum = reference.oldChecksum.GetOrElse("(none)");
				var newChecksum = reference.newChecksum.GetOrElse("(none)");
				sb.AppendLine($" - {bundleName}: {oldChecksum} -> {newChecksum}");
			}
			Log.Warning(sb.ToString());
		}
	}

	/// <summary>Log the high-level fields of a pull request (id, status, source, submitter).</summary>
	public static void PrintInfo(ProposedManifest pr)
	{
		if (pr == null)
		{
			return;
		}

		var sb = new StringBuilder();
		sb.AppendLine($"Pull request {pr.id.GetOrElse("<unknown>")}");
		sb.AppendLine($" - status: {(pr.status.HasValue ? pr.status.Value.ToString() : "<unknown>")}");
		sb.AppendLine($" - source scope: {pr.sourceScopeId.GetOrElse("(explicit manifest)")}");
		sb.AppendLine($" - submitted by account: {pr.submittedByAccountId.GetOrElse(0)}");
		if (pr.resultManifestId.HasValue)
		{
			sb.AppendLine($" - result manifest: {pr.resultManifestId.Value}");
		}
		if (pr.lastApplyError.HasNonEmptyValue)
		{
			sb.AppendLine($" - last apply error: {pr.lastApplyError.Value}");
		}
		Log.Information(sb.ToString());
	}

	private static void PrintComponentChanges(ManifestDiffEntry[] components, ManifestDiffChange change, string verb)
	{
		var matching = components.Where(c => c.change.HasValue && c.change.Value == change).ToArray();
		if (matching.Length == 0)
		{
			return;
		}

		var noun = matching.Length == 1 ? "component" : "components";
		var sb = new StringBuilder();
		sb.AppendLine($"{verb} {matching.Length} {noun}");
		foreach (var entry in matching)
		{
			var kind = entry.kind.GetOrElse("component");
			var name = entry.name.GetOrElse("<unknown>");
			sb.AppendLine($" - [{kind}] {name}");
		}
		Log.Warning(sb.ToString());
	}
}
