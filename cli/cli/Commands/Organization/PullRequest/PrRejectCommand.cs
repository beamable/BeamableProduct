using Beamable.Api.Autogenerated.Beamopullrequest;
using Beamable.Server;
using System.CommandLine;

namespace cli;

public class PrRejectCommandArgs : CommandArgs
{
	public string Id;
}

public class PrRejectCommandOutput
{
	public string id;
}

public class PrRejectCommand : AtomicCommand<PrRejectCommandArgs, PrRejectCommandOutput>, ISkipManifest
{
	public PrRejectCommand() : base("reject", "Reject a pending pull request")
	{
	}

	public override void Configure()
	{
		AddOption(new Option<string>("--id", "The id of the pull request to reject"),
			(args, i) => args.Id = i);
	}

	public override async Task<PrRejectCommandOutput> GetResult(PrRejectCommandArgs args)
	{
		if (string.IsNullOrEmpty(args.Id))
		{
			throw new CliException("--id is required. Use --id <pullRequestId> to specify the pull request to reject.");
		}

		var api = args.Provider.GetService<IBeamBeamopullrequestApi>();
		await api.PostPrsReject(args.Id);

		return new PrRejectCommandOutput { id = args.Id };
	}
}
