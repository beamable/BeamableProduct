using Beamable.Api.Autogenerated.Beamobundle;
using Beamable.Api.Autogenerated.Models;
using Beamable.Server;
using System.Linq;

namespace cli.DeploymentCommands.Bundles;

public class ListBundlesCommandArgs : CommandArgs
{
}

public class ListBundlesCommandOutput
{
	public BundleInfo[] bundles;
}

public class ListBundlesCommand : AtomicCommand<ListBundlesCommandArgs, ListBundlesCommandOutput>, ISkipManifest
{
	public ListBundlesCommand() : base("list", "List bundles visible to the current realm")
	{
	}

	public override void Configure()
	{
	}

	public override async Task<ListBundlesCommandOutput> GetResult(ListBundlesCommandArgs args)
	{
		var api = args.Provider.GetService<IBeamBeamobundleApi>();
		var response = await api.GetBundles();
		return new ListBundlesCommandOutput
		{
			bundles = (response.bundles ?? System.Array.Empty<Bundle>()).Select(BundleInfo.FromBundle).ToArray()
		};
	}
}
