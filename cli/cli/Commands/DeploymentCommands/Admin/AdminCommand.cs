using Beamable.Api.Autogenerated.Beamobundle;
using Beamable.Api.Autogenerated.Beamoforcedbundle;
using Beamable.Api.Autogenerated.Models;
using Beamable.Server;
using cli.BundleCommands;
using cli.Services.Bundles;
using System.CommandLine;

namespace cli.DeploymentCommands.Admin;

public class AdminCommand : CommandGroup
{
	public AdminCommand() : base("admin", "Beamable-internal deploy administration commands")
	{
	}
}

public class ForceInjectBundleCommandArgs : CommandArgs
{
	public string bundleName;
	public string ns;
	public string cid;
	public string realmId;
	public string checksum;
	public bool unset;
}

public class ForceInjectBundleCommandOutput
{
	public string bundleName;
	public string cid;
	public string realmId;
	public string checksum;
	public bool removed;
}

/// <summary>
/// Set (or clear) a forced-bundle override — the admin escape hatch used by Beamable's
/// portal-extension PR-preview workflow (UC13). When <c>--realm</c> is passed the override is
/// scoped to that realm; otherwise it is applied CID-wide as the default for every realm.
/// Requires a Beamable SuperAdmin server token; a customer admin token is insufficient server-side.
/// </summary>
public class ForceInjectBundleCommand : AtomicCommand<ForceInjectBundleCommandArgs, ForceInjectBundleCommandOutput>
{
	public override bool IsForInternalUse => true;

	public ForceInjectBundleCommand() : base("force-inject",
		"Force-inject a bundle checksum onto a realm or CID-wide (Beamable-internal)")
	{
	}

	public override void Configure()
	{
		AddArgument(new Argument<string>("bundle-name", "The bundle name"),
			(args, i) => args.bundleName = i);
		AddOption(new Option<string>(new[] { "--namespace", "-ns" }, "The bundle's namespace (a customer alias). Defaults to the current context's customer alias; pass it to force-inject a bundle owned by a different customer"),
			(args, i) => args.ns = i);
		AddOption(new Option<string>(new[] { "--target-cid" }, "The target customer id to force the bundle onto. Defaults to the current project's cid; pass it to force-inject onto a different customer"),
			(args, i) => args.cid = i);
		AddOption(new Option<string>(new[] { "--target-realm", "-r" }, "The target realm id to force the bundle onto. When omitted, the bundle is forced CID-wide as the default for every realm"),
			(args, i) => args.realmId = i);
		AddOption(new Option<string>(new[] { "--checksum", "-c" }, "The bundle content checksum to force (sha256:<checksum>). Defaults to the bundle's latest published checksum when omitted"),
			(args, i) => args.checksum = i);
		AddOption(new Option<bool>(new[] { "--unset" }, "Remove the force-injection override instead of setting it"),
			(args, i) => args.unset = i);
	}

	public override async Task<ForceInjectBundleCommandOutput> GetResult(ForceInjectBundleCommandArgs args)
	{
		BundleWorkspace.ValidateName(args.bundleName);
		var name = args.bundleName;
		var ns = string.IsNullOrEmpty(args.ns) ? await BundleNamespace.Get(args) : BundleNamespace.FromAlias(args.ns);
		var fullName = BundleNamespace.Qualify(ns, name);
		var cid = string.IsNullOrEmpty(args.cid) ? args.AppContext.Cid : args.cid;
		var isRealmScoped = !string.IsNullOrEmpty(args.realmId);
		var api = args.Provider.GetService<IBeamBeamoforcedbundleApi>();

		if (args.unset)
		{
			if (isRealmScoped)
			{
				await api.DeleteRealmsForcedBundles(name, cid, ns, args.realmId);
			}
			else
			{
				await api.DeleteCidsForcedBundles(name, cid, ns);
			}

			return new ForceInjectBundleCommandOutput
			{
				bundleName = fullName, cid = cid, realmId = args.realmId, removed = true
			};
		}

		var checksum = args.checksum;
		if (string.IsNullOrEmpty(checksum))
		{
			var bundleApi = args.Provider.GetService<IBeamBeamobundleApi>();
			var latest = await bundleApi.GetBundles(name, ns);
			checksum = BundleInfo.FromBundle(latest?.bundle)?.checksum;
			if (string.IsNullOrEmpty(checksum))
				throw new CliException($"Could not resolve the latest published checksum for [{fullName}]; pass --checksum explicitly.");
		}

		if (isRealmScoped)
		{
			await api.PutRealmsForcedBundles(name, cid, ns, args.realmId, new SetForcedBundleRequest { checksum = checksum });
		}
		else
		{
			await api.PutCidsForcedBundles(name, cid, ns, new SetForcedBundleRequest { checksum = checksum });
		}

		return new ForceInjectBundleCommandOutput
		{
			bundleName = fullName, cid = cid, realmId = args.realmId, checksum = checksum, removed = false
		};
	}
}
