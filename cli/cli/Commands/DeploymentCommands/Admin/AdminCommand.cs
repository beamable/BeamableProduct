using Beamable.Api.Autogenerated.Beamoforcedbundle;
using Beamable.Api.Autogenerated.Models;
using Beamable.Server;
using cli.Services.Bundles;
using System.CommandLine;

namespace cli.DeploymentCommands.Admin;

public class AdminCommand : CommandGroup
{
	public AdminCommand() : base("admin", "Beamable-internal deploy administration commands")
	{
	}
}

public class ForceInjectBundleCommandArgs : CommandArgs
{
	public string bundleName;
	public string realmId;
	public string checksum;
	public bool unset;
}

public class ForceInjectBundleCommandOutput
{
	public string bundleName;
	public string realmId;
	public string checksum;
	public bool removed;
}

/// <summary>
/// Set (or clear) a per-realm forced-bundle override — the admin escape hatch used by Beamable's
/// portal-extension PR-preview workflow (UC13). Requires a Beamable SuperAdmin server token; a
/// customer admin token is insufficient server-side.
/// </summary>
public class ForceInjectBundleCommand : AtomicCommand<ForceInjectBundleCommandArgs, ForceInjectBundleCommandOutput>
{
	public override bool IsForInternalUse => true;

	public ForceInjectBundleCommand() : base("force-inject",
		"Force-inject a bundle checksum onto a specific realm (Beamable-internal)")
	{
	}

	public override void Configure()
	{
		AddArgument(new Argument<string>("bundle-name", "The namespaced bundle name, e.g. <namespace>/<bundle-name>"),
			(args, i) => args.bundleName = i);
		AddOption(new Option<string>(new[] { "--realm", "-r" }, "The target realm id to force the bundle onto"),
			(args, i) => args.realmId = i);
		AddOption(new Option<string>(new[] { "--checksum", "-c" }, "The bundle content checksum to force (sha256:<checksum>)"),
			(args, i) => args.checksum = i);
		AddOption(new Option<bool>(new[] { "--unset" }, "Remove the per-realm force-injection override instead of setting it"),
			(args, i) => args.unset = i);
	}

	public override async Task<ForceInjectBundleCommandOutput> GetResult(ForceInjectBundleCommandArgs args)
	{
		if (string.IsNullOrEmpty(args.realmId))
			throw new CliException("--realm is required.");

		var (ns, name) = BundleWorkspace.SplitBundleName(args.bundleName);
		var cid = args.AppContext.Cid;
		var api = args.Provider.GetService<IBeamBeamoforcedbundleApi>();

		if (args.unset)
		{
			await api.DeleteRealmsForcedBundles(name, cid, ns, args.realmId);
			return new ForceInjectBundleCommandOutput
			{
				bundleName = args.bundleName, realmId = args.realmId, removed = true
			};
		}

		if (string.IsNullOrEmpty(args.checksum))
			throw new CliException("--checksum is required unless --unset is passed.");

		await api.PutRealmsForcedBundles(name, cid, ns, args.realmId, new SetForcedBundleRequest { checksum = args.checksum });
		return new ForceInjectBundleCommandOutput
		{
			bundleName = args.bundleName, realmId = args.realmId, checksum = args.checksum, removed = false
		};
	}
}
