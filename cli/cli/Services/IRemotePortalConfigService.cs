using System.Text.Json;
using System.Text.RegularExpressions;
using Beamable.Api.Autogenerated.Beamo;
using Beamable.Api.Autogenerated.Content;
using Beamable.Api.Autogenerated.Models;
using Beamable.Common.Content;
using cli.Services;
using cli.Services.PortalExtension;
using Beamable.Server;
using Newtonsoft.Json.Linq;
using JsonSerializer = System.Text.Json.JsonSerializer;

namespace cli.Portal;

public interface IRemotePortalConfigService
{
	Task<RemotePortalConfiguration> GetRemotePortalConfig(CommandArgs args);
}

public class RemotePortalConfigService : IRemotePortalConfigService
{
	private const string MetadataAssetFileName = "metadata.json";

	/// <summary>How long a downloaded remote metadata.json stays valid in the on-disk cache.</summary>
	private static readonly TimeSpan MetadataCacheTtl = TimeSpan.FromMinutes(20);

	/// <summary>Sub-folder under <c>.beamable/temp</c> holding cached remote extension metadata.</summary>
	private const string MetadataCacheFolderName = "portal-extension-metadata";

	public async Task<RemotePortalConfiguration> GetRemotePortalConfig(CommandArgs args)
	{
		var config = await FetchRemotePortalConfig(args);
		await AppendRunningExtensionMountSites(args, config);
		return config;
	}

	/// <summary>
	/// Fetch the remote mount-site catalog (extension-pages.json) and strip the common realm path
	/// prefix. This is best-effort: if the catalog is unavailable — e.g. an environment that
	/// doesn't publish it (404) or no network — we warn and return an empty config so that
	/// locally-discovered extension mount sites still surface instead of failing the whole command.
	/// </summary>
	private static async Task<RemotePortalConfiguration> FetchRemotePortalConfig(CommandArgs args)
	{
		var url = "http://localhost:4950/" + "/extension-pages.json";
		try
		{
			var client = new HttpClient();
			var json = await client.GetStringAsync(url);
			var config = JsonSerializer.Deserialize<RemotePortalConfiguration>(json, new JsonSerializerOptions
			{
				IncludeFields = true
			}) ?? new RemotePortalConfiguration();

			var commonPref = "/:customerId/games/:gameId/realms/:realmId/";
			foreach (var mountSite in config.mountSites)
			{
				if (mountSite.path.StartsWith(commonPref))
				{
					mountSite.path = mountSite.path.Substring(commonPref.Length);
				}
			}

			return config;
		}
		catch (Exception e)
		{
			Log.Warning(
				"Could not fetch remote portal config from {url}: {message}. Continuing with local extension mount sites only.",
				url, e.Message);
			return new RemotePortalConfiguration();
		}
	}

	/// <summary>
	/// A Portal Extension can expose slots for *other* extensions to mount into via the
	/// <c>&lt;BeamExtensionSite selector="..." /&gt;</c> React component. Those slots aren't in the
	/// remote extension-pages.json — the hosted Portal derives them from each extension's built
	/// metadata at runtime, and the CLI mirrors that here: we gather every currently-running
	/// extension's metadata (remote preferred over local), turn each into a mount site at the URL the
	/// extension renders, and merge them in. Best-effort: any failure logs and leaves the remote
	/// config intact.
	/// </summary>
	private static async Task AppendRunningExtensionMountSites(CommandArgs args, RemotePortalConfiguration config)
	{
		try
		{
			var metadatas = await GatherRunningExtensionMetadata(args);

			var sites = new List<RemotePortalConfiguration.MountSiteConfig>();
			foreach (var metadata in metadatas)
				sites.AddRange(BuildMountSitesFromMetadata(metadata));

			MergeMountSites(config.mountSites, sites);
		}
		catch (Exception e)
		{
			Log.Warning("Could not append running extension mount sites: {message}", e.Message);
		}
	}

	/// <summary>
	/// Collect the build metadata of every running portal extension. Remote (deployed) extensions are
	/// the source of truth: when an extension is running both remotely and locally we use the remote
	/// metadata and only fall back to the local on-disk build when there is no remote instance.
	/// </summary>
	private static async Task<List<ExtensionBuildMetaData>> GatherRunningExtensionMetadata(CommandArgs args)
	{
		var result = new List<ExtensionBuildMetaData>();

		// Local portal-extension defs (a built one has assets/metadata.json on disk).
		var localDefsByName = new Dictionary<string, PortalExtensionDef>();
		foreach (var def in args.BeamoLocalSystem.BeamoManifest.ServiceDefinitions
			         .Where(x => x.PortalExtensionDefinition?.Properties?.IsPortalExtension ?? false)
			         .Select(x => x.PortalExtensionDefinition))
		{
			if (!string.IsNullOrEmpty(def?.Name))
				localDefsByName[def.Name] = def;
		}

		// Remote (deployed) extensions that are running (enabled && !archived), from the V2 manifest.
		var remoteRefsByName = new Dictionary<string, BeamoV2PortalExtensionReference>();
		try
		{
			var beamoApi = args.Provider.GetService<IBeamBeamoApi>();
			var v2Manifest = await beamoApi.GetManifestsCurrent();
			if (v2Manifest.portalExtensionReferences.TryGet(out var refs))
			{
				foreach (var reference in refs)
				{
					var name = reference.name.GetOrElse("");
					if (string.IsNullOrEmpty(name)) continue;
					var enabled = reference.enabled.GetOrElse(true);
					var archived = reference.archived.GetOrElse(false);
					if (!enabled || archived) continue;
					remoteRefsByName[name] = reference;
				}
			}
		}
		catch (Exception e)
		{
			Log.Debug("Could not fetch V2 manifest for portal extensions: {message}", e.Message);
		}

		var remoteMetaByName = await DownloadRemoteMetadata(args, remoteRefsByName);

		var names = new HashSet<string>(localDefsByName.Keys);
		names.UnionWith(remoteRefsByName.Keys);

		foreach (var name in names)
		{
			// Remote wins.
			if (remoteMetaByName.TryGetValue(name, out var remoteMeta) && remoteMeta != null)
			{
				result.Add(remoteMeta);
				continue;
			}

			if (localDefsByName.TryGetValue(name, out var localDef))
			{
				var localMeta = TryLoadLocalMetadata(localDef);
				if (localMeta != null) result.Add(localMeta);
			}
		}

		return result;
	}

	/// <summary>
	/// Resolve and download each remote extension's metadata.json from the private content system,
	/// honoring a 20-minute on-disk cache under <c>.beamable/temp</c>. All cache-miss downloads share a
	/// single <c>PostBinaryPrivateUrls</c> call; each download is independently best-effort.
	/// </summary>
	private static async Task<Dictionary<string, ExtensionBuildMetaData>> DownloadRemoteMetadata(
		CommandArgs args, Dictionary<string, BeamoV2PortalExtensionReference> remoteRefsByName)
	{
		var resolved = new Dictionary<string, ExtensionBuildMetaData>();
		if (remoteRefsByName.Count == 0) return resolved;

		// (name, contentId, version, cacheFile) tuples still needing a live download.
		var pending = new List<(string name, string contentId, string version, string cacheFile)>();

		foreach (var (name, reference) in remoteRefsByName)
		{
			var fileRef = reference.files.GetOrElse(Array.Empty<BeamoV2ExtensionContentReference>())
				.FirstOrDefault(f => f.name.GetOrElse("") == MetadataAssetFileName);
			if (fileRef == null) continue;

			var contentId = fileRef.contentId.GetOrElse("");
			var version = fileRef.version.GetOrElse("");
			if (string.IsNullOrEmpty(contentId)) continue;

			var cacheFile = GetCacheFilePath(args, name, version);
			var cached = TryReadCache(cacheFile);
			if (cached != null)
			{
				resolved[name] = cached;
				continue;
			}

			pending.Add((name, contentId, version, cacheFile));
		}

		if (pending.Count == 0) return resolved;

		try
		{
			var contentApi = args.Provider.GetService<IContentApi>();
			var request = new GetBinaryDownloadUrlsRequest
			{
				// expirationSeconds left unset → server default.
				requests = pending
					.Select(p => new GetContentRequest { contentId = p.contentId, version = p.version })
					.ToArray()
			};

			var response = await contentApi.PostBinaryPrivateUrls(request);
			var urlByContentId = (response.urls ?? Array.Empty<BinaryContentReference>())
				.Where(u => !string.IsNullOrEmpty(u.contentId) && !string.IsNullOrEmpty(u.url))
				.GroupBy(u => u.contentId)
				.ToDictionary(g => g.Key, g => g.First().url);

			using var client = new HttpClient();
			foreach (var item in pending)
			{
				try
				{
					if (!urlByContentId.TryGetValue(item.contentId, out var url)) continue;

					var json = await client.GetStringAsync(url);
					var metadata = DeserializeMetadata(json);
					if (metadata == null) continue;

					WriteCache(item.cacheFile, json);
					resolved[item.name] = metadata;
				}
				catch (Exception e)
				{
					Log.Debug("Could not download metadata for extension {name}: {message}", item.name, e.Message);
				}
			}
		}
		catch (Exception e)
		{
			Log.Warning("Could not resolve remote extension metadata download urls: {message}", e.Message);
		}

		return resolved;
	}

	private static ExtensionBuildMetaData TryLoadLocalMetadata(PortalExtensionDef def)
	{
		try
		{
			var path = Path.Combine(def.AbsolutePath, "assets", MetadataAssetFileName);
			if (!File.Exists(path)) return null;
			return DeserializeMetadata(File.ReadAllText(path));
		}
		catch (Exception e)
		{
			Log.Debug("Could not read local metadata for extension {name}: {message}", def?.Name, e.Message);
			return null;
		}
	}

	/// <summary>
	/// Deserialize an extension's metadata.json into just the parts the mount-site builder needs
	/// (name, declared sites, and each mount's page + nav labels). Read via <c>JObject</c> rather than
	/// <c>JsonConvert.DeserializeObject&lt;ExtensionBuildMetaData&gt;</c> on purpose: the package
	/// properties carry an <c>OptionalConverter</c> that throws on a JSON <c>null</c> (e.g. an unset
	/// <c>navGroupOrder</c>), which would abort the whole parse. Failproof: missing/extra fields and any
	/// parse error yield null so the caller skips that extension instead of failing the command.
	/// </summary>
	public static ExtensionBuildMetaData DeserializeMetadata(string json)
	{
		if (string.IsNullOrWhiteSpace(json)) return null;
		try
		{
			var root = JObject.Parse(json);

			var metadata = new ExtensionBuildMetaData
			{
				Name = (string)root["Name"],
				ToolkitVersion = (string)root["ToolkitVersion"],
				ExtensionSites = (root["ExtensionSites"] as JArray)?
					.Select(t => (string)t)
					.Where(s => !string.IsNullOrEmpty(s))
					.ToList(),
				Properties = new PortalExtensionPackageProperties()
			};

			if ((root["Properties"] as JObject)?["mounts"] is JArray mountsArray)
			{
				metadata.Properties.Mounts = new List<PortalExtensionMountProperties>();
				foreach (var mount in mountsArray.OfType<JObject>())
				{
					metadata.Properties.Mounts.Add(new PortalExtensionMountProperties
					{
						Page = (string)mount["page"],
						NavGroup = OptionalStringFromToken(mount["navGroup"]),
						NavLabel = OptionalStringFromToken(mount["navLabel"]),
					});
				}
			}

			return metadata;
		}
		catch (Exception e)
		{
			Log.Debug("Could not parse extension metadata.json: {message}", e.Message);
			return null;
		}
	}

	private static OptionalString OptionalStringFromToken(JToken token)
	{
		var value = token == null || token.Type == JTokenType.Null ? null : (string)token;
		return string.IsNullOrEmpty(value)
			? new OptionalString()
			: new OptionalString { HasValue = true, Value = value };
	}

	// --- caching -----------------------------------------------------------------------------------

	private static string GetCacheFilePath(CommandArgs args, string name, string version)
	{
		var dir = Path.Combine(args.ConfigService.ConfigTempDirectoryPath, MetadataCacheFolderName);
		// Key by name + version (content checksum) so a redeploy naturally misses the stale entry.
		var fileName = $"{Sanitize(name)}_{Sanitize(version)}.json";
		return Path.Combine(dir, fileName);
	}

	private static string Sanitize(string value)
	{
		if (string.IsNullOrEmpty(value)) return "_";
		foreach (var c in Path.GetInvalidFileNameChars())
			value = value.Replace(c, '_');
		return value;
	}

	private static ExtensionBuildMetaData TryReadCache(string cacheFile)
	{
		try
		{
			if (!File.Exists(cacheFile)) return null;
			if (DateTime.UtcNow - File.GetLastWriteTimeUtc(cacheFile) > MetadataCacheTtl) return null;
			return DeserializeMetadata(File.ReadAllText(cacheFile));
		}
		catch (Exception e)
		{
			Log.Debug("Could not read cached extension metadata {file}: {message}", cacheFile, e.Message);
			return null;
		}
	}

	private static void WriteCache(string cacheFile, string json)
	{
		try
		{
			var dir = Path.GetDirectoryName(cacheFile);
			if (!string.IsNullOrEmpty(dir)) Directory.CreateDirectory(dir);
			File.WriteAllText(cacheFile, json);
		}
		catch (Exception e)
		{
			Log.Debug("Could not write cached extension metadata {file}: {message}", cacheFile, e.Message);
		}
	}

	// --- pure builders -----------------------------------------------------------------------------

	/// <summary>
	/// Turn one extension's metadata into the mount site(s) its slots live at — one per entry in the
	/// extension's <c>mounts</c> (an extension can render in more than one place). Each site's
	/// <c>path</c> is the URL that mount renders at (its <c>page</c>); selectors are the extension's
	/// declared <c>BeamExtensionSite</c>s as <c>#&lt;name&gt;</c> component slots (the same for every
	/// mount); navContext is that mount's navGroup/navLabel (the hub hierarchy itself lives in the page
	/// path, and the nav group is an additional grouping within a hub). Returns empty when the extension
	/// declares no slots, has no name, or has no mount with a resolvable page.
	/// </summary>
	public static List<RemotePortalConfiguration.MountSiteConfig> BuildMountSitesFromMetadata(ExtensionBuildMetaData metadata)
	{
		var sites = new List<RemotePortalConfiguration.MountSiteConfig>();

		if (string.IsNullOrEmpty(metadata?.Name)) return sites;
		if (metadata.ExtensionSites == null || metadata.ExtensionSites.Count == 0) return sites;

		var mounts = metadata.Properties?.Mounts;
		if (mounts == null || mounts.Count == 0) return sites;

		// The exposed selectors are declared in source, so they are identical at every mount location.
		var selectorNames = new List<string>();
		var seen = new HashSet<string>();
		foreach (var raw in metadata.ExtensionSites)
		{
			if (string.IsNullOrEmpty(raw)) continue;
			var selector = raw.StartsWith("#") ? raw : "#" + raw;
			if (seen.Add(selector)) selectorNames.Add(selector);
		}
		if (selectorNames.Count == 0) return sites;

		foreach (var mount in mounts)
		{
			var path = BeamoLocalSystem.NormalizePortalExtensionMountPage(mount?.Page);
			if (string.IsNullOrEmpty(path)) continue;

			var navContext = new List<string>();
			var navGroup = mount.NavGroup;
			var navLabel = mount.NavLabel;
			if (navGroup != null && navGroup.HasValue && !string.IsNullOrEmpty(navGroup.Value))
				navContext.Add(navGroup.Value);
			if (navLabel != null && navLabel.HasValue && !string.IsNullOrEmpty(navLabel.Value))
				navContext.Add(navLabel.Value);

			sites.Add(new RemotePortalConfiguration.MountSiteConfig
			{
				path = path,
				// fresh selector instances per site — MergeMountSites mutates the target's list.
				selectors = selectorNames
					.Select(s => new RemotePortalConfiguration.MountSiteSelector { selector = s, type = "component" })
					.ToList(),
				navContext = navContext
			});
		}

		return sites;
	}

	/// <summary>
	/// Merge generated sites into the existing list, keyed by <c>path</c>: selectors of additions for an
	/// existing path are unioned in, de-duplicated by selector string. A duplicate selector at the same
	/// path means two extensions chose the same (non-namespaced) selector name — we keep the first and
	/// <c>Log.Warning</c> the collision. navContext is filled from the first addition that has one.
	/// </summary>
	public static void MergeMountSites(
		List<RemotePortalConfiguration.MountSiteConfig> target,
		IEnumerable<RemotePortalConfiguration.MountSiteConfig> additions)
	{
		foreach (var addition in additions)
		{
			var existing = target.FirstOrDefault(s => s.path == addition.path);
			if (existing == null)
			{
				target.Add(addition);
				continue;
			}

			foreach (var selector in addition.selectors)
			{
				if (existing.selectors.Any(s => s.selector == selector.selector))
				{
					WarnDuplicateSelector(selector.selector, addition.path);
					continue;
				}
				existing.selectors.Add(selector);
			}

			if ((existing.navContext == null || existing.navContext.Count == 0) &&
			    addition.navContext != null && addition.navContext.Count > 0)
			{
				existing.navContext = addition.navContext;
			}
		}
	}

	// Guarded: the global logger may be uninitialized/disposed in some contexts (e.g. unit tests),
	// and a duplicate-selector warning must never break the merge itself.
	private static void WarnDuplicateSelector(string selector, string path)
	{
		try
		{
			Log.Warning(
				"Duplicate mount-site selector '{selector}' at path '{path}'. Keeping the first; give each BeamExtensionSite a unique selector name.",
				selector, path);
		}
		catch
		{
			// ignored
		}
	}

	private const string ExtensionSiteTagName = "BeamExtensionSite";

	// Matches the `selector` attribute (and only that attribute — the lookbehind rejects `data-selector`
	// / `fooSelector`) within a single tag's body, in any of the common JSX forms:
	//   selector="x"   selector='x'   selector={'x'}   selector={"x"}
	private static readonly Regex SelectorAttrRegex = new Regex(
		"(?<![\\w-])selector\\s*=\\s*(?:\"([^\"]*)\"|'([^']*)'|\\{\\s*(?:\"([^\"]*)\"|'([^']*)')\\s*\\})",
		RegexOptions.Compiled);

	/// <summary>
	/// Scan an extension's source tree for <c>BeamExtensionSite</c> usages and return every declared
	/// selector name (e.g. <c>top</c>, <c>B-top</c>) in discovery order, de-duplicated. Run once at
	/// build time; the result is persisted into metadata.json. I/O wrapper around
	/// <see cref="ParseExtensionSiteSelectors"/>.
	/// </summary>
	// Source-file extensions that can declare a <BeamExtensionSite>.
	private static readonly HashSet<string> ExtensionSiteSourceExtensions =
		new(StringComparer.OrdinalIgnoreCase) { ".tsx", ".jsx", ".ts" };

	// Directories that never hold first-party extension source. These are pruned DURING the
	// traversal in EnumerateExtensionSourceFiles rather than post-filtered. Pruning
	// node_modules is essential, not cosmetic: a file:-linked shared library is symlinked
	// into node_modules, and a recursive walk that descended into it would follow that
	// symlink into the library's own tree — walking unrelated files and, on a symlink loop,
	// throwing mid-enumeration. That throw would abort CreateMetaDataFile before the build's
	// error is even recorded (and is swallowed entirely on the file-watcher path), so a
	// library-referencing extension would silently never register with the portal.
	private static readonly HashSet<string> ExtensionSiteExcludedDirs =
		new(StringComparer.OrdinalIgnoreCase) { "node_modules", "dist", "assets", "build", ".git" };

	public static List<string> ScanExtensionSiteSelectors(string extAbsolutePath)
	{
		var found = new List<string>();
		var seen = new HashSet<string>();

		if (!string.IsNullOrEmpty(extAbsolutePath) && Directory.Exists(extAbsolutePath))
		{
			foreach (var file in EnumerateExtensionSourceFiles(extAbsolutePath))
			{
				string text;
				try { text = File.ReadAllText(file); }
				catch { continue; }

				foreach (var selector in ParseExtensionSiteSelectors(text))
					if (seen.Add(selector))
						found.Add(selector);
			}
		}

		return found;
	}

	/// <summary>
	/// Depth-first walk of an extension's own source tree, pruning non-source directories
	/// (see <see cref="ExtensionSiteExcludedDirs"/>) and skipping symlinks/junctions so a
	/// file:-linked shared library under node_modules is never traversed. Yields only source
	/// files that can contain a <c>&lt;BeamExtensionSite&gt;</c>.
	/// </summary>
	private static IEnumerable<string> EnumerateExtensionSourceFiles(string root)
	{
		var visited = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
		var stack = new Stack<string>();
		stack.Push(Path.GetFullPath(root));

		while (stack.Count > 0)
		{
			var dir = stack.Pop();
			if (!visited.Add(dir)) continue;

			IEnumerable<FileSystemInfo> entries;
			try { entries = new DirectoryInfo(dir).EnumerateFileSystemInfos(); }
			catch { continue; } // unreadable directory — skip

			foreach (var entry in entries)
			{
				// Never follow symlinks/junctions — a file:-linked library lives behind one.
				if ((entry.Attributes & FileAttributes.ReparsePoint) != 0) continue;

				if ((entry.Attributes & FileAttributes.Directory) != 0)
				{
					if (!ExtensionSiteExcludedDirs.Contains(entry.Name))
						stack.Push(entry.FullName);
					continue;
				}

				if (ExtensionSiteSourceExtensions.Contains(entry.Extension))
					yield return entry.FullName;
			}
		}
	}

	/// <summary>
	/// Pure parse: extract the <c>selector</c> value of every <c>&lt;BeamExtensionSite&gt;</c> in a
	/// single source file. Robust to arbitrary other props in any order (e.g. <c>mountKind</c>),
	/// multiline tags, and props whose values contain <c>&gt;</c> (such as arrow functions
	/// <c>onReady={() =&gt; x()}</c>) — the tag boundary is found brace/quote-aware, not via
	/// <c>[^&gt;]</c>. Accepts <c>selector="x"</c>, <c>selector='x'</c> and the expression forms
	/// <c>selector={'x'}</c> / <c>selector={"x"}</c>. Selector names are arbitrary (author-chosen);
	/// new props on the component never affect extraction.
	/// </summary>
	public static IEnumerable<string> ParseExtensionSiteSelectors(string fileText)
	{
		if (string.IsNullOrEmpty(fileText)) yield break;

		var tag = "<" + ExtensionSiteTagName;
		var searchFrom = 0;
		while (true)
		{
			var open = fileText.IndexOf(tag, searchFrom, StringComparison.Ordinal);
			if (open < 0) yield break;

			var tagBodyStart = open + tag.Length;
			// Reject longer names like <BeamExtensionSiteFoo: the next char must end the tag name.
			if (tagBodyStart < fileText.Length)
			{
				var c = fileText[tagBodyStart];
				if (!char.IsWhiteSpace(c) && c != '/' && c != '>')
				{
					searchFrom = tagBodyStart;
					continue;
				}
			}

			var tagEnd = FindOpeningTagEnd(fileText, tagBodyStart);
			var body = fileText.Substring(tagBodyStart, tagEnd - tagBodyStart);

			var match = SelectorAttrRegex.Match(body);
			if (match.Success)
			{
				for (var g = 1; g < match.Groups.Count; g++)
				{
					if (match.Groups[g].Success && !string.IsNullOrEmpty(match.Groups[g].Value))
					{
						yield return match.Groups[g].Value;
						break;
					}
				}
			}

			searchFrom = tagEnd;
		}
	}

	/// <summary>
	/// Return the index just past the <c>&gt;</c> that closes the opening tag starting at
	/// <paramref name="start"/>, treating <c>&gt;</c> inside string literals or <c>{...}</c>
	/// expressions (e.g. an arrow function <c>=&gt;</c>) as part of the tag rather than its end.
	/// </summary>
	private static int FindOpeningTagEnd(string text, int start)
	{
		var braceDepth = 0;
		var quote = '\0';
		for (var i = start; i < text.Length; i++)
		{
			var c = text[i];
			if (quote != '\0')
			{
				if (c == quote) quote = '\0';
				continue;
			}
			switch (c)
			{
				case '"':
				case '\'':
				case '`':
					quote = c;
					break;
				case '{':
					braceDepth++;
					break;
				case '}':
					if (braceDepth > 0) braceDepth--;
					break;
				case '>':
					if (braceDepth == 0) return i + 1;
					break;
			}
		}
		return text.Length;
	}
}
