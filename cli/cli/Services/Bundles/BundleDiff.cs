using Beamable.Api.Autogenerated.Models;
using Beamable.Server;
using cli.Deployment.Services;
using System;
using System.Collections.Generic;
using System.Linq;

namespace cli.Services.Bundles;

/// <summary>One component-level change between a locally-built bundle and the published one.</summary>
public class BundleComponentChange
{
	public string name;
	public string kind;   // "service" | "storage"
	public string change; // "Added" | "Removed" | "Changed"
	public string localChecksum = "";
	public string publishedChecksum = "";
}

public class BundleDiffResult
{
	/// <summary>True when the built bundle is identical to the published @latest (publishing would be a no-op).</summary>
	public bool isNoOp;

	/// <summary>The published @latest bundle checksum, or empty if the bundle was never published.</summary>
	public string publishedChecksum = "";

	public List<BundleComponentChange> changes = new List<BundleComponentChange>();
}

/// <summary>
/// Diffs a locally-built bundle against the published one at the component level (by name/id,
/// comparing per-component checksums — which the shared checksum code stamps during the build). The
/// component diff is the source of truth; the bundle-level checksum is only a summary indicator.
/// </summary>
public static class BundleDiff
{
	public static BundleDiffResult Compute(
		BeamoBasicServiceReference[] localServices,
		BeamoBasicServiceStorageReference[] localStorages,
		List<PortalExtensionPlanReference> localExtensions,
		Bundle published)
	{
		var result = new BundleDiffResult();

		var pubServices = new Dictionary<string, string>();
		var pubStorages = new Dictionary<string, string>();
		var pubExtensions = new Dictionary<string, string>();
		if (published != null)
		{
			result.publishedChecksum = published.checksum.GetOrElse("");
			// Fully-qualify: the enclosing cli.Services namespace has its own ServiceReference/
			// ServiceStorageReference (BeamoService.cs) that would otherwise shadow the Models ones.
			foreach (var s in published.serviceReferences.GetOrElse(Array.Empty<Beamable.Api.Autogenerated.Models.ServiceReference>()))
				pubServices[s.serviceName.GetOrElse("")] = s.checksum.GetOrElse("");
			foreach (var st in published.storageReferences.GetOrElse(Array.Empty<Beamable.Api.Autogenerated.Models.ServiceStorageReference>()))
				pubStorages[st.id.GetOrElse("")] = st.checksum.GetOrElse("");
			// Portal extensions: compare by per-file MD5 versions (the same representation both sides
			// store). The PE-level checksum is NOT comparable — the client computes a SHA-256 build
			// hash while the server stores its own MD5, so they'd never match.
			foreach (var e in published.portalExtensionReferences.GetOrElse(Array.Empty<Beamable.Api.Autogenerated.Models.PortalExtensionReference>()))
				pubExtensions[e.name.GetOrElse("")] = PublishedExtensionFingerprint(e);
		}

		DiffSide(result, localServices.Select(s => (s.serviceName, s.checksum)), pubServices, "service");
		DiffSide(result, localStorages.Select(s => (s.id, s.checksum)), pubStorages, "storage");
		DiffSide(result, localExtensions.Select(e => (e.name, LocalExtensionFingerprint(e))), pubExtensions, "extension");

		result.isNoOp = result.changes.Count == 0;
		return result;
	}

	private static void DiffSide(
		BundleDiffResult result,
		IEnumerable<(string name, string checksum)> local,
		Dictionary<string, string> published,
		string kind)
	{
		var localNames = new HashSet<string>();
		foreach (var (name, checksum) in local)
		{
			localNames.Add(name);
			var localChecksum = checksum ?? "";
			if (!published.TryGetValue(name, out var pub))
			{
				result.changes.Add(new BundleComponentChange { name = name, kind = kind, change = "Added", localChecksum = localChecksum });
			}
			else if (pub != localChecksum)
			{
				result.changes.Add(new BundleComponentChange { name = name, kind = kind, change = "Changed", localChecksum = localChecksum, publishedChecksum = pub });
			}
		}

		foreach (var kv in published)
		{
			if (!localNames.Contains(kv.Key))
				result.changes.Add(new BundleComponentChange { name = kv.Key, kind = kind, change = "Removed", publishedChecksum = kv.Value });
		}
	}

	// A portal extension's identity for diffing = its files' MD5 versions (order-independent).
	private static string LocalExtensionFingerprint(PortalExtensionPlanReference e) =>
		string.Join("|", (e.files ?? new List<PortalExtensionPlanFileReference>())
			.OrderBy(f => f.name, StringComparer.Ordinal)
			.Select(f => $"{f.name}:{f.version}"));

	private static string PublishedExtensionFingerprint(Beamable.Api.Autogenerated.Models.PortalExtensionReference e) =>
		string.Join("|", e.files.GetOrElse(Array.Empty<Beamable.Api.Autogenerated.Models.ExtensionContentReference>())
			.OrderBy(f => f.name.GetOrElse(""), StringComparer.Ordinal)
			.Select(f => $"{f.name.GetOrElse("")}:{f.version.GetOrElse("")}"));

	/// <summary>Log a human-readable summary of the diff.</summary>
	public static void Print(BundleDiffResult diff, string bundleName)
	{
		if (diff.isNoOp)
		{
			Log.Information($"No changes — [{bundleName}] is identical to the published @latest.");
			return;
		}

		Log.Information($"Plan for [{bundleName}] — {diff.changes.Count} change(s):");
		foreach (var c in diff.changes)
		{
			Log.Information($"  {c.change,-8} {c.kind,-8} {c.name}");
		}
	}
}
