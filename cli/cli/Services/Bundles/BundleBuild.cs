using Beamable.Api.Autogenerated.Beamobundle;
using Beamable.Api.Autogenerated.Models;
using Beamable.Common.Api;
using cli.Deployment.Services;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace cli.Services.Bundles;

/// <summary>
/// Shared build/catalog helpers for <c>bundles plan</c> and <c>bundles publish</c> so the two can't
/// drift. <see cref="SelectComponents"/> pulls the built local components belonging to a bundle out
/// of a full deploy plan and enforces the bundle invariants.
/// </summary>
public static class BundleBuild
{
	/// <summary>
	/// Select the built services/storages/portal-extensions belonging to <paramref name="bundle"/>
	/// from a deploy plan. Throws if the bundle lists a component that wasn't built as a local
	/// service/storage/extension.
	/// </summary>
	public static (BeamoBasicServiceReference[] services, BeamoBasicServiceStorageReference[] storages, List<PortalExtensionPlanReference> extensions)
		SelectComponents(DeployablePlan plan, BundleConfigFile bundle)
	{
		var componentSet = new HashSet<string>(bundle.components);

		var services = (plan.manifest.manifest ?? Array.Empty<BeamoBasicServiceReference>())
			.Where(s => componentSet.Contains(s.serviceName)).ToArray();
		var storages = plan.manifest.storageReference.GetOrElse(Array.Empty<BeamoBasicServiceStorageReference>())
			.Where(st => componentSet.Contains(st.id)).ToArray();
		var extensions = plan.portalExtensionReferences
			.Where(pe => componentSet.Contains(pe.name)).ToList();

		var found = new HashSet<string>(services.Select(s => s.serviceName)
			.Concat(storages.Select(s => s.id))
			.Concat(extensions.Select(e => e.name)));
		var missing = bundle.components.Where(c => !found.Contains(c)).ToList();
		if (missing.Count > 0)
		{
			throw new CliException(
				$"Bundle=[{bundle.name}] lists components not built as local services/storages/extensions: {string.Join(", ", missing)}");
		}

		return (services, storages, extensions);
	}

	/// <summary>Fetch the catalog's latest published entry for a bundle, or null if it was never published.</summary>
	public static async Task<Bundle> FetchLatestPublished(IBeamBeamobundleApi api, string ns, string name)
	{
		try
		{
			Bundle bundle = (await api.GetBundles(name, ns)).bundle; // implicit Optional<Bundle> -> Bundle
			return bundle;
		}
		catch (RequesterException e) when (e.Status == 404)
		{
			return null;
		}
	}
}
