	public bool WasCreated() => !string.IsNullOrEmpty(FullFilePath) && string.IsNullOrEmpty(OldFullFilePath);
	public bool WasRenamed() => !WasDeleted() && !WasCreated() && FullFilePath != OldFullFilePath;
	public bool WasChanged() => !WasCreated() && !WasDeleted() && FullFilePath == OldFullFilePath;

	/// <summary>
	/// Parses a file name (without extension) of the form "createdDate¬manifestUid" into its parts.
	/// </summary>
	public static bool TryParseEntryFileName(string fileNameWithoutExtension, out long createdDate, out string manifestUid)
	{
		createdDate = 0;
		manifestUid = "";
		if (string.IsNullOrEmpty(fileNameWithoutExtension)) return false;

		var parts = fileNameWithoutExtension.Split(ENTRY_FILE_NAME_SEPARATOR);
		if (parts.Length != 2) return false;
		if (!long.TryParse(parts[0], out createdDate)) return false;
		manifestUid = parts[1];
		return true;
	}

	/// <summary>
	/// Builds the file name (without extension) from a createdDate and manifestUid.
	/// </summary>
	public static string BuildEntryFileName(long createdDate, string manifestUid) => $"{createdDate}{ENTRY_FILE_NAME_SEPARATOR}{manifestUid}";
}

