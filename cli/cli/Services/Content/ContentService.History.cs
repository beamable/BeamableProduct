	/// <summary>
	/// Monitors filesystem changes to content history entry files and streams change events asynchronously.
	/// Creates a FileSystemWatcher to track Created, Deleted, Changed, and Renamed events for JSON files in the entries directory.
	/// Changes are batched together (100ms window) to handle multiple simultaneous file modifications.
	/// </summary>
	/// <param name="pid">The realm id (PID) whose content history entries to monitor</param>
	/// <param name="manifestId">The manifest ID to monitor, defaults to "global"</param>
	/// <param name="token">Cancellation token to stop monitoring</param>
	/// <returns>
	/// An async enumerable of LocalContentHistoryEntryFileChanges, each containing a batch of file change events.
	/// Yields batches of changes as they occur with a 100ms debounce window.
	/// </returns>
	/// <remarks>
	/// Edge cases:
	/// - Uses event disambiguation based on LastWrite timestamps to prevent duplicate events (Windows fires both Created and Changed for new files)
	/// - Only one watcher per (pid, manifestId) tuple is stored in the dictionary (overwrites if called multiple times)
	/// - Breaks and yields when cancellation is requested
	/// - All file paths must end with .json extension
	/// - Renamed events include both old and new file information
	/// </remarks>
	public async IAsyncEnumerable<LocalContentHistoryEntryFileChanges> ListenToContentHistoryEntries(string pid, string manifestId = "global", [EnumeratorCancellation] CancellationToken token = default)
	{
		// Make sure the folder we're supposed to be watching exists.
		var contentFolder = EnsureContentHistoryEntriesFolderExists(out _, _requester.Pid, manifestId);

		// Create the file watcher
		_contentHistoryEntryWatchers.Add((pid, manifestId), new FileSystemWatcher());

		// We need to keep track of the "LastWrite" time for each content entry to disambiguate multiple events firing simultaneously for the same file.
		// For example, on Windows, a Created event is always fired alongside a Changed event of the same file (because of "reasons", I guess)
		// This dictionary helps us fire off only one relevant event at a time even when the file watcher gets confused about the desirable semantics.
		var eventDisambiguationHelper = new Dictionary<string, long>();

		// Set up the file watcher...
		var watcher = _contentHistoryEntryWatchers[(pid, manifestId)];
		watcher.BeginInit();
		watcher.Path = contentFolder;
		watcher.IncludeSubdirectories = true;
		watcher.Filter = "*.json";
		watcher.EnableRaisingEvents = true;
		watcher.NotifyFilter = (NotifyFilters.Attributes |
		                        NotifyFilters.CreationTime |
		                        NotifyFilters.DirectoryName |
		                        NotifyFilters.FileName |
		                        NotifyFilters.LastWrite);
		watcher.Error += (_, e) => Log.Error(e.GetException().ToString());
		watcher.Disposed += (_, e) => Log.Error($"Disposed {e}");
		watcher.Created += (_, e) => { OnFilesChanged(e); };
		watcher.Deleted += (_, e) => { OnFilesChanged(e); };
		watcher.Renamed += (_, e) => { OnFilesChanged(e); };
		watcher.Changed += (_, e) => { OnFilesChanged(e); };
		watcher.EndInit();

		// Keep waiting for messages from the set up file watcher.
		var reader = _channelContentHistoryEntries.Reader;

		while (true)
		{
			// If the operation was canceled, we break out.
			if (token.IsCancellationRequested)
			{
				break;
			}

			try
			{
				await reader.WaitToReadAsync(token);

				// For the case which multiple files are edited at the same time, the watcher triggers one message for each one.
				// So to batch it together, we wait 100 ms and then try to read from the buffer.
				await Task.Delay(100, token);
			}
			catch (OperationCanceledException)
			{
				Log.Debug($"{nameof(ListenToLocalContentFileChanges)} was cancelled");
				break;
			}

			// Get all the files that were changed respecting the PID filter.
			var batchedChanges = new LocalContentHistoryEntryFileChanges() { AllFileChanges = new() };
			while (reader.TryRead(out var changed))
			{
				batchedChanges.AllFileChanges.Add(changed);
			}


			yield return batchedChanges;
		}

		yield break;

		void OnFilesChanged(FileSystemEventArgs e)
		{
			var lastWriteTime = new DateTimeOffset(File.GetLastWriteTime(e.FullPath)).ToUnixTimeMilliseconds();
			if (e.ChangeType == WatcherChangeTypes.Created)
			{
				var changedEntry = new ChangedContentHistoryEntryFile { OwnerPid = pid };
				changedEntry.OwnerPid = pid;
				changedEntry.OwnerManifestId = manifestId;

				ChangedContentHistoryEntryFile.TryParseEntryFileName(Path.GetFileNameWithoutExtension(e.Name), out var timestamp, out var uid);
				changedEntry.UnixTimestamp = timestamp;
				changedEntry.ManifestUid = uid;
				changedEntry.FullFilePath = e.FullPath;
				changedEntry.OldUnixTimestamp = 0L;
				changedEntry.OldManifestUid = "";
				changedEntry.OldFullFilePath = "";

				if (!eventDisambiguationHelper.TryGetValue(e.FullPath, out var time) || time != lastWriteTime)
				{
					eventDisambiguationHelper[e.FullPath] = lastWriteTime;
					Log.Verbose($"Created file ({lastWriteTime}): {JsonConvert.SerializeObject(changedEntry)} ");
					_channelContentHistoryEntries.Writer.TryWrite(changedEntry);
				}
			}
			else if (e.ChangeType == WatcherChangeTypes.Deleted)
			{
				var changedContent = new ChangedContentHistoryEntryFile { OwnerPid = pid };
				changedContent.OwnerPid = pid;
				changedContent.OwnerManifestId = manifestId;

				ChangedContentHistoryEntryFile.TryParseEntryFileName(Path.GetFileNameWithoutExtension(e.Name), out var timestamp, out var uid);
				changedContent.UnixTimestamp = changedContent.OldUnixTimestamp = timestamp;
				changedContent.ManifestUid = changedContent.OldManifestUid = uid;
				changedContent.FullFilePath = "";
				changedContent.OldFullFilePath = e.FullPath;
				Log.Verbose($"Deleted file: {JsonConvert.SerializeObject(changedContent)} ");
				_channelContentHistoryEntries.Writer.TryWrite(changedContent);
			}
			else if (e.ChangeType == WatcherChangeTypes.Changed)
			{
				var changedContent = new ChangedContentHistoryEntryFile { OwnerPid = pid };
				changedContent.OwnerPid = pid;
				changedContent.OwnerManifestId = manifestId;

				ChangedContentHistoryEntryFile.TryParseEntryFileName(Path.GetFileNameWithoutExtension(e.Name), out var timestamp, out var uid);
				changedContent.UnixTimestamp = changedContent.OldUnixTimestamp = timestamp;
				changedContent.ManifestUid = changedContent.OldManifestUid = uid;
				changedContent.FullFilePath = changedContent.OldFullFilePath = e.FullPath;

				if (!eventDisambiguationHelper.TryGetValue(e.FullPath, out var time) || time != lastWriteTime)
				{
					eventDisambiguationHelper[e.FullPath] = lastWriteTime;
					Log.Verbose($"Changed file ({lastWriteTime}): {JsonConvert.SerializeObject(changedContent)} ");
					_channelContentHistoryEntries.Writer.TryWrite(changedContent);
				}
			}
			else if (e.ChangeType == WatcherChangeTypes.Renamed)
			{
				if (e is RenamedEventArgs renamedEventArgs)
				{
					var renamedContent = new ChangedContentHistoryEntryFile();
					renamedContent.OwnerPid = pid;
					renamedContent.OwnerManifestId = manifestId;

					ChangedContentHistoryEntryFile.TryParseEntryFileName(Path.GetFileNameWithoutExtension(renamedEventArgs.Name), out var newTimestamp, out var newUid);
					renamedContent.UnixTimestamp = newTimestamp;
					renamedContent.ManifestUid = newUid;
					renamedContent.FullFilePath = renamedEventArgs.FullPath;

					ChangedContentHistoryEntryFile.TryParseEntryFileName(Path.GetFileNameWithoutExtension(renamedEventArgs.OldName), out var oldTimestamp, out var oldUid);
					renamedContent.OldUnixTimestamp = oldTimestamp;
					renamedContent.OldManifestUid = oldUid;
					renamedContent.OldFullFilePath = renamedEventArgs.OldFullPath;
					Log.Verbose($"Renamed file: {JsonConvert.SerializeObject(renamedContent)} ");

					_channelContentHistoryEntries.Writer.TryWrite(renamedContent);
				}
			}
		}
	}

	/// <summary>
	/// Monitors filesystem changes to content history changelist files and streams change events asynchronously.
	/// Creates a FileSystemWatcher to track Created, Deleted, Changed, and Renamed events for JSON files in the changelists directory.
	/// Distinguishes between read-only access (LastAccessTime change) and modification (LastWriteTime change) for Changed events.
	/// Changes are batched together (100ms window) to handle multiple simultaneous file modifications.
	/// </summary>
	/// <param name="pid">The realm id (PID) whose content history changelists to monitor</param>
	/// <param name="manifestId">The manifest ID to monitor, defaults to "global"</param>
	/// <param name="token">Cancellation token to stop monitoring</param>
	/// <returns>
	/// An async enumerable of LocalContentHistoryChangelistFileChanges, each containing a batch of file change events.
	/// Yields batches of changes as they occur with a 100ms debounce window.
	/// </returns>
	/// <remarks>
	/// Edge cases:
	/// - Tracks both LastAccessTime and LastWriteTime to distinguish reads vs writes on Changed events
	/// - Uses event disambiguation to prevent duplicate events (same file, same timestamp)
	/// - Sets WasReadAccessed=true when only access time changed, false when write time changed
	/// - Only one watcher per (pid, manifestId) tuple is stored in the dictionary
	/// - Breaks and yields when cancellation is requested
	/// - All file paths must end with .json extension
	/// - Renamed events include both old and new file information
	/// </remarks>
	public async IAsyncEnumerable<LocalContentHistoryChangelistFileChanges> ListenToContentHistoryChangelists(string pid, string manifestId = "global", [EnumeratorCancellation] CancellationToken token = default)
	{
		// Make sure the folder we're supposed to be watching exists.
		var changelistsFolder = EnsureContentHistoryChangelistsFolderExists(out _, pid, manifestId);

		// Create the file watcher
		_contentHistoryChangelistWatchers.Add((pid, manifestId), new FileSystemWatcher());

		// Disambiguation helper for simultaneous events
		var eventDisambiguationHelper = new Dictionary<string, long>();

		// Set up the file watcher...
		var watcher = _contentHistoryChangelistWatchers[(pid, manifestId)];
		watcher.BeginInit();
		watcher.Path = changelistsFolder;
		watcher.IncludeSubdirectories = true;
		watcher.Filter = "*.json";
		watcher.EnableRaisingEvents = true;
		watcher.NotifyFilter = (NotifyFilters.Attributes |
		                        NotifyFilters.CreationTime |
		                        NotifyFilters.DirectoryName |
		                        NotifyFilters.FileName |
		                        NotifyFilters.LastWrite |
		                        NotifyFilters.LastAccess);
		watcher.Error += (_, e) => Log.Error(e.GetException().ToString());
		watcher.Disposed += (_, e) => Log.Error($"Disposed {e}");
		watcher.Created += (_, e) => { OnFilesChanged(e); };
		watcher.Deleted += (_, e) => { OnFilesChanged(e); };
		watcher.Renamed += (_, e) => { OnFilesChanged(e); };
		watcher.Changed += (_, e) => { OnFilesChanged(e); };
		watcher.EndInit();

		// Keep waiting for messages from the set up file watcher.
		var reader = _channelContentHistoryChangelists.Reader;

		while (true)
		{
			if (token.IsCancellationRequested)
			{
				break;
			}

			try
			{
				await reader.WaitToReadAsync(token);
				await Task.Delay(100, token);
			}
			catch (OperationCanceledException)
			{
				Log.Debug($"{nameof(ListenToContentHistoryChangelists)} was cancelled");
				break;
			}

			var batchedChanges = new LocalContentHistoryChangelistFileChanges() { AllFileChanges = new() };
			while (reader.TryRead(out var changed))
			{
				batchedChanges.AllFileChanges.Add(changed);
			}

			yield return batchedChanges;
		}

		yield break;

		void OnFilesChanged(FileSystemEventArgs e)
		{
			var lastWriteTime = new DateTimeOffset(File.GetLastWriteTime(e.FullPath)).ToUnixTimeMilliseconds();
			var lastAccessTime = new DateTimeOffset(File.GetLastAccessTime(e.FullPath)).ToUnixTimeMilliseconds();
			if (e.ChangeType == WatcherChangeTypes.Created)
			{
				var changed = new ChangedContentHistoryChangelistFile { OwnerPid = pid };
				changed.OwnerPid = pid;
				changed.OwnerManifestId = manifestId;

				changed.ManifestUid = Path.GetFileNameWithoutExtension(e.Name) ?? "";
				changed.FullFilePath = e.FullPath;
				changed.OldFullFilePath = "";
				changed.OldManifestUid = "";

				if (!eventDisambiguationHelper.TryGetValue(e.FullPath, out var time) || time != lastWriteTime)
				{
					eventDisambiguationHelper[e.FullPath] = lastWriteTime;
					Log.Verbose($"Created changelist file ({lastWriteTime}): {JsonConvert.SerializeObject(changed)} ");
					_channelContentHistoryChangelists.Writer.TryWrite(changed);
				}
			}
			else if (e.ChangeType == WatcherChangeTypes.Deleted)
			{
				var changed = new ChangedContentHistoryChangelistFile { OwnerPid = pid };
				changed.OwnerPid = pid;
				changed.OwnerManifestId = manifestId;

				changed.ManifestUid = "";
				changed.OldManifestUid = Path.GetFileNameWithoutExtension(e.Name) ?? "";
				changed.FullFilePath = "";
				changed.OldFullFilePath = e.FullPath;
				Log.Verbose($"Deleted changelist file: {JsonConvert.SerializeObject(changed)} ");
				_channelContentHistoryChangelists.Writer.TryWrite(changed);
			}
			else if (e.ChangeType == WatcherChangeTypes.Changed)
			{
				var changed = new ChangedContentHistoryChangelistFile { OwnerPid = pid };
				changed.OwnerPid = pid;
				changed.OwnerManifestId = manifestId;

				changed.ManifestUid = Path.GetFileNameWithoutExtension(e.Name) ?? "";
				changed.OldManifestUid = changed.ManifestUid;
				changed.FullFilePath = changed.OldFullFilePath = e.FullPath;

				// Check if this is a read access change (access time changed but write time didn't)
				var accessKey = $"{e.FullPath}_access";
				var writeKey = e.FullPath;

				bool accessChanged = !eventDisambiguationHelper.TryGetValue(accessKey, out var lastKnownAccessTime) || lastKnownAccessTime != lastAccessTime;
				bool writeChanged = !eventDisambiguationHelper.TryGetValue(writeKey, out var lastKnownWriteTime) || lastKnownWriteTime != lastWriteTime;

				if (accessChanged && !writeChanged)
				{
					// Only access time changed - this is a read
					changed.WasReadAccessed = true;
					eventDisambiguationHelper[accessKey] = lastAccessTime;
					Log.Verbose($"Read changelist file ({lastAccessTime}): {JsonConvert.SerializeObject(changed)} ");
					_channelContentHistoryChangelists.Writer.TryWrite(changed);
				}
				else if (writeChanged)
				{
					// Write time changed - this is a modification
					changed.WasReadAccessed = false;
					eventDisambiguationHelper[writeKey] = lastWriteTime;
					eventDisambiguationHelper[accessKey] = lastAccessTime;
					Log.Verbose($"Changed changelist file ({lastWriteTime}): {JsonConvert.SerializeObject(changed)} ");
					_channelContentHistoryChangelists.Writer.TryWrite(changed);
				}
			}
			else if (e.ChangeType == WatcherChangeTypes.Renamed)
			{
				if (e is RenamedEventArgs renamedEventArgs)
				{
					var renamed = new ChangedContentHistoryChangelistFile();
					renamed.OwnerPid = pid;
					renamed.OwnerManifestId = manifestId;
					renamed.ManifestUid = Path.GetFileNameWithoutExtension(renamedEventArgs.Name) ?? "";
					renamed.OldManifestUid = Path.GetFileNameWithoutExtension(renamedEventArgs.OldName) ?? "";
					renamed.FullFilePath = renamedEventArgs.FullPath;
					renamed.OldFullFilePath = renamedEventArgs.OldFullPath;
					Log.Verbose($"Renamed changelist file: {JsonConvert.SerializeObject(renamed)} ");

					_channelContentHistoryChangelists.Writer.TryWrite(renamed);
				}
			}
		}
	}


	/// <summary>
	/// Synchronizes content history entries from the remote Beamable API to local storage, fetching only new entries not already cached.
	/// Walks backwards through history in pages until all new entries are downloaded or cancellation occurs.
	/// Invokes an optional progress callback after each page is processed.
	/// </summary>
	/// <param name="pid">The realm id (PID) to sync entries for</param>
	/// <param name="manifestId">The manifest ID to sync, defaults to "global"</param>
	/// <param name="onProgress">Optional callback invoked after each page is fetched and saved, receiving a report of the newly fetched entries</param>
	/// <param name="cancellationToken">Cancellation token to abort the sync operation</param>
	/// <returns>A ContentHistoryFetchReport containing all entries fetched during this sync session</returns>
	/// <remarks>
	/// Edge cases:
	/// - Uses semaphore to prevent file system races during concurrent writes
	/// - Skips entries that already exist locally (based on CreatedDate)
	/// - Starts from the current time or continues from the oldest local entry (localStartDate - 1)
	/// - Stops when hasMore=false or when minCreatedInPage doesn't advance (all pages exhausted)
	/// - Logs errors for individual entry save failures but continues with remaining entries
	/// - Returns partial results if cancellation occurs mid-sync
	/// - Page limit is hardcoded to 100 entries per API call
	/// </remarks>
	public async Task<ContentHistoryFetchReport> SyncContentHistoryEntries(string pid, string manifestId = "global", Action<ContentHistoryFetchReport> onProgress = null, CancellationToken cancellationToken = default)
	{
		// Ensure local entries folder exists
		var entriesPath = EnsureContentHistoryEntriesFolderExists(out _, pid, manifestId);

		// Load locally cached entries so we can avoid re-fetching duplicates and determine where to continue from
		var allLocallyCachedEntries = await GetAllContentHistoryLocalEntries(pid, manifestId);
		var existingCreatedDates = new HashSet<long>();
		long? localStartDate = null;
		if (allLocallyCachedEntries.Entries.Count > 0)
		{
			existingCreatedDates.UnionWith(allLocallyCachedEntries.Entries.Select(e => e.CreatedDate));
			localStartDate = allLocallyCachedEntries.StartDate;
		}

		// Collector for all entries we actually fetched during this call
		var allFetchedEntries = new List<ContentHistoryEntry>();

		int greedyPageLimit = 100;
		bool hasMore = true;
		long toDateCursor = localStartDate.HasValue ? localStartDate.Value - 1 : DateTimeOffset.UtcNow.ToUnixTimeMilliseconds();

		try
		{
			while (hasMore && !cancellationToken.IsCancellationRequested)
			{
				var fetchResult = await _contentApi.GetManifestDiffs(manifestId, toDate: toDateCursor, limit: greedyPageLimit, offset: 0);

				long minCreatedInPage = long.MaxValue;
				var pageEntries = new List<ContentHistoryEntry>();

				// Acquire entries semaphore while writing files for this page to avoid races with readers/listeners
				await _contentHistoryEntriesFileSystemSemaphore.WaitAsync(cancellationToken);
				try
				{
					foreach (var diff in fetchResult.diffs)
					{
						if (diff == null) continue;
						var createdDate = diff.createdDate;
						minCreatedInPage = Math.Min(minCreatedInPage, createdDate);
						if (existingCreatedDates.Contains(createdDate)) continue;

						var entry = ContentHistoryEntry.FromManifestDiff(diff);

						var entryFileName = ChangedContentHistoryEntryFile.BuildEntryFileName(entry.CreatedDate, entry.ManifestUid);
						var fileName = Path.Combine(entriesPath, $"{entryFileName}.json");
						try
						{
							var json = JsonSerializer.Serialize(entry, GetContentFileSerializationOptions());
							await DirectoryUtils.WriteUtf8FileAsync(fileName, json, cancellationToken);

							existingCreatedDates.Add(createdDate);
							pageEntries.Add(entry);
							allFetchedEntries.Add(entry);
						}
						catch (Exception e)
						{
							Log.Error(e, $"Failed to save content history entry for createdDate={createdDate}");
						}
					}
				}
				finally
				{
					_contentHistoryEntriesFileSystemSemaphore.Release();
				}

				// Report progress for this page
				if (onProgress != null)
				{
					onProgress(new ContentHistoryFetchReport() { Entries = pageEntries });
				}

				// Prepare cursor for next page: go strictly older than the minimum createdDate we saw
				if (minCreatedInPage == long.MaxValue)
				{
					hasMore = false;
				}
				else
				{
					toDateCursor = minCreatedInPage - 1;
					hasMore = fetchResult.hasMore;
				}
			}
		}
		catch (OperationCanceledException)
		{
			Log.Debug($"{nameof(SyncContentHistoryEntries)} was cancelled");
		}

		// Return a report containing all fetched entries
		return new ContentHistoryFetchReport() { Entries = allFetchedEntries };
	}

	/// <summary>
	/// Loads all locally cached content history entries from disk, with optional filtering by date range and manifest UIDs.
	/// Files are filtered by filename pattern before deserialization for efficiency.
	/// Entries are sorted by CreatedDate in descending order (most recent first).
	/// </summary>
	/// <param name="pid">The realm id (PID) whose entries to load</param>
	/// <param name="manifest">The manifest ID, defaults to "global"</param>
	/// <param name="startDate">Optional minimum CreatedDate (inclusive) to filter by</param>
	/// <param name="endDate">Optional maximum CreatedDate (inclusive) to filter by</param>
	/// <param name="manifestUids">Optional collection of manifest UIDs to filter by (if provided, only matching entries are loaded)</param>
	/// <returns>
	/// A ContentHistoryEntriesPage containing all matching entries, sorted by CreatedDate descending (newest first).
	/// StartDate field contains the oldest CreatedDate (last entry), EndDate field contains the newest CreatedDate (first entry).
	/// ContainedManifestUids array contains all unique manifest UIDs in the result set.
	/// </returns>
	/// <remarks>
	/// Edge cases:
	/// - Uses semaphore to prevent concurrent file system access
	/// - Retries file reads in a busy-wait loop if IOException occurs (file locked by another process)
	/// - Returns empty page if no entries match the filters
	/// - Throws if any entry file fails to deserialize (logs errors for AggregateException)
	/// - All files must have .json extension
	/// - Filename parsing must succeed for TryParseEntryFileName, otherwise file is skipped
	/// </remarks>
	public async Task<ContentHistoryEntriesPage> GetAllContentHistoryLocalEntries(string pid, string manifest = "global", long? startDate = null, long? endDate = null, IEnumerable<string> manifestUids = null)
	{
		// Ensure exclusive access while reading existing entry files to avoid races with writers
		await _contentHistoryEntriesFileSystemSemaphore.WaitAsync();
		try
		{
			var localEntriesPage = new ContentHistoryEntriesPage();
			var entriesPath = EnsureContentHistoryEntriesFolderExists(out _, pid, manifest);

			// Convert manifestUids to HashSet for O(1) lookups if provided
			var uidsFilter = manifestUids != null ? new HashSet<string>(manifestUids) : null;

			var tasks =
				// For each existing file inside the content folder we care about, we'll have one entry.
				Directory.EnumerateFiles(entriesPath)
					// Only get the JSON files
					.Where(fp => Path.GetExtension(fp).EndsWith("json"))
					// Filter by date range and UIDs based on filename before reading/deserializing
					.Where(fp =>
					{
						var fileNameWithoutExt = Path.GetFileNameWithoutExtension(fp);
						if (!ChangedContentHistoryEntryFile.TryParseEntryFileName(fileNameWithoutExt, out var createdDate, out var uid))
							return false;

						// Apply date range filter
						if (startDate.HasValue && createdDate < startDate.Value)
							return false;
						if (endDate.HasValue && createdDate > endDate.Value)
							return false;

						// Apply UID filter
						if (uidsFilter != null && !uidsFilter.Contains(uid))
							return false;

						return true;
					})
					// Load the files that passed the filter into memory
					.Select(async fp =>
					{
						// We got to do this so that we don't collide with other software editing the files on disk.
						var text = "";
						var readText = false;
						while (!readText || text == "")
						{
							try
							{
								text = await File.ReadAllTextAsync(fp);
								readText = true;
							}
							catch (IOException)
							{
								readText = false;
								text = "";
							}
						}

						try
						{
							var json = JsonSerializer.Deserialize<JsonElement>(text, GetContentFileSerializationOptions());
							var entryFile = new ContentHistoryEntry()
							{
								ManifestUid = json.GetProperty(ContentHistoryEntry.JSON_PROP_MANIFEST_UID).GetString(),
								CreatedDate = json.GetProperty(ContentHistoryEntry.JSON_PROP_CREATED_DATE).GetInt64(),
								PublishedBy = json.GetProperty(ContentHistoryEntry.JSON_PROP_PUBLISHED_BY).GetString() ?? "",
								PublishedByName = json.GetProperty(ContentHistoryEntry.JSON_PROP_PUBLISHED_BY_NAME).GetString() ?? "",
								AffectedContentIds = json.TryGetProperty(ContentHistoryEntry.JSON_PROP_AFFECTED_CONTENT_IDS, out var affectedContentIds)
									? affectedContentIds.EnumerateArray().Select(e => e.GetString()).ToArray()
									: Array.Empty<string>(),
							};

							return entryFile;
						}
						catch (Exception e)
						{
							Log.Error(e, $"Error when loading content history entry file {fp}");
							throw;
						}
					}).ToList();

			// Wait for all the content file tasks and return the list of ContentHistoryEntry.
			try
			{
				localEntriesPage.Entries = (await Task.WhenAll(tasks)).ToList();
				if (localEntriesPage.Entries.Count > 0)
				{
					localEntriesPage.Entries.Sort((e1, e2) => e2.CreatedDate.CompareTo(e1.CreatedDate)); // Descending order (most recent first)
					localEntriesPage.ContainedManifestUids = localEntriesPage.Entries.Select(e => e.ManifestUid).ToArray();
					localEntriesPage.StartDate = localEntriesPage.Entries.Last().CreatedDate; // Last is oldest
					localEntriesPage.EndDate = localEntriesPage.Entries.First().CreatedDate; // First is newest
				}

				return localEntriesPage;
			}
			catch (Exception e)
			{
				if (e is AggregateException ae)
				{
					foreach (var aeInnerException in ae.InnerExceptions)
					{
						Log.Error(aeInnerException, "Error when loading content history entry files");
					}
				}

				throw;
			}
		}
		finally
		{
			_contentHistoryEntriesFileSystemSemaphore.Release();
		}
	}


	/// <summary>
	/// Synchronizes content history changelists from the remote Beamable CDN to local storage, downloading only changelists that aren't already cached.
	/// Loads local entries to determine changelist URLs, filters out already-cached changelists, downloads remaining ones in parallel, and saves them to disk.
	/// Post-processes each changelist to normalize null values, compute file paths, and extract metadata.
	/// </summary>
	/// <param name="pid">The realm id (PID) to sync changelists for</param>
	/// <param name="manifestId">The manifest ID to sync</param>
	/// <param name="manifestUids">The collection of manifest UIDs whose changelists should be synced</param>
	/// <param name="cancellationToken">Cancellation token to abort the sync operation</param>
	/// <returns>A ContentHistoryChangelistPage containing all changelists downloaded during this sync session</returns>
	/// <remarks>
	/// Edge cases:
	/// - Uses semaphore to prevent file system races during concurrent writes
	/// - Skips changelists that already exist locally
	/// - Only downloads changelists for UIDs that have corresponding entries in the local cache
	/// - Downloads all changelists in parallel using Task.WhenAll
	/// - Replaces null Created/Removed/Modified dictionaries with empty dictionaries
	/// - Normalizes all string fields in changelist entries to empty strings if null (to avoid null checks downstream)
	/// - Computes JsonFilePath as relative path from Beamable workspace
	/// - Parses content ID to extract TypeName and Name (splits on last '.' character)
	/// - Sets ChangeStatus based on which dictionary the entry appears in (Created/Modified/Removed)
	/// - Logs errors for individual download/save failures and throws to propagate the exception
	/// - Returns partial results if cancellation occurs mid-sync
	/// </remarks>
	public async Task<ContentHistoryChangelistPage> SyncContentHistoryChangelists(string pid, string manifestId, IEnumerable<string> manifestUids, CancellationToken cancellationToken = default)
	{
		// Ensure local changelists folder exists
		var changelistsPath = EnsureContentHistoryChangelistsFolderExists(out _, pid, manifestId);

		// Convert the enumeration to a HashSet for faster lookups
		var desiredUids = new HashSet<string>(manifestUids);

		// Load locally cached entries to find the changelist URLs
		var allLocallyCachedEntries = await GetAllContentHistoryLocalEntries(pid, manifestId);
		var changelistUrlsByUid = new Dictionary<string, string>();

		if (allLocallyCachedEntries?.Entries != null)
		{
			foreach (var entry in allLocallyCachedEntries.Entries)
			{
				if (desiredUids.Contains(entry.ManifestUid))
				{
					changelistUrlsByUid[entry.ManifestUid] = GetContentHistoryChangelistUrl(_requester.Cid, _requester.Pid, entry);
				}
			}
		}

		// Load locally cached changelists to exclude entries that are already stored
		var allLocallyCachedChangelists = await GetAllContentHistoryLocalChangelists(pid, manifestId);
		var localChangelistUids = new HashSet<string>();

		if (allLocallyCachedChangelists?.Changelists != null)
		{
			foreach (var changelist in allLocallyCachedChangelists.Changelists)
			{
				localChangelistUids.Add(changelist.ManifestUid);
			}
		}

		// Determine which changelists need to be downloaded
		var changelistsToDownload = new List<string>();
		foreach (var uid in desiredUids)
		{
			if (!localChangelistUids.Contains(uid) && changelistUrlsByUid.ContainsKey(uid))
			{
				changelistsToDownload.Add(uid);
			}
		}

		// Collector for all changelists we actually downloaded during this call
		var downloadedChangelists = new List<ContentHistoryChangelist>();

		try
		{
			// Download the changelist JSON files
			var downloadTasks = changelistsToDownload.Select(async uid =>
			{
				try
				{
					var url = changelistUrlsByUid[uid];
					var changelist = await _requester.CustomRequest<ContentHistoryChangelist>(Method.GET, url,
						parser: (jsonString) => JsonSerializer.Deserialize<ContentHistoryChangelist>(jsonString, GetContentFileSerializationOptions()));

					return (uid, changelist);
				}
				catch (Exception e)
				{
					Log.Error(e, $"Failed to download content history changelist for uid={uid}");
					throw;
				}
			}).ToList();

			var downloadResults = await Task.WhenAll(downloadTasks);

			// Acquire changelists semaphore while writing files to avoid races with readers/listeners
			await _contentHistoryChangelistsFileSystemSemaphore.WaitAsync(cancellationToken);
			try
			{
				foreach (var (uid, changelist) in downloadResults)
				{
					var fixedChangelist = changelist;
					fixedChangelist.ManifestUid = uid;

					// We make sure we don't store "null"s here ---
					// this is just to make it so that we don't need to null check and because UE's JSON library emits an annoying warning when it tries to get a string out of a "null".
					fixedChangelist.Created ??= new();
					fixedChangelist.Removed ??= new();
					fixedChangelist.Modified ??= new();

					// Compute the base path for content files in this manifest
					var contentBasePath = Path.Combine(ContentHistoryContentPath, pid, manifestId, uid);

					void PostProcessEntry(Dictionary<string, ContentHistoryChangelistEntry> dict, ContentStatus changeStatus)
					{
						foreach (string key in dict.Keys)
						{
							var e = dict[key];
							e.OldVersion ??= "";
							e.OldChecksum ??= "";
							e.OldTags ??= Array.Empty<string>();
							e.NewVersion ??= "";
							e.NewChecksum ??= "";
							e.NewTags ??= Array.Empty<string>();
							// Compute the expected local file path for this content (relative to BeamableWorkspace)
							var absolutePath = Path.Combine(contentBasePath, $"{key}.json");
							e.JsonFilePath = Path.GetRelativePath(_config.BeamableWorkspace, absolutePath);
							// Compute metadata fields
							e.FullId = key;
							// Split content ID (e.g., "items.MyItem") into type ("items") and name ("MyItem")
							var lastDotIndex = key.LastIndexOf('.');
							if (lastDotIndex > 0)
							{
								e.TypeName = key.Substring(0, lastDotIndex);
								e.Name = key.Substring(lastDotIndex + 1);
							}
							else
							{
								e.TypeName = "";
								e.Name = key;
							}

							e.ChangeStatus = (int)changeStatus;
							e.ChangeDate = fixedChangelist.PublishedAt;
							dict[key] = e;
						}
					}

					PostProcessEntry(fixedChangelist.Created, ContentStatus.Created);
					PostProcessEntry(fixedChangelist.Removed, ContentStatus.Deleted);
					PostProcessEntry(fixedChangelist.Modified, ContentStatus.Modified);

					var fileName = Path.Combine(changelistsPath, $"{uid}.json");
					try
					{
						var json = JsonSerializer.Serialize(fixedChangelist, GetContentFileSerializationOptions());
						await DirectoryUtils.WriteUtf8FileAsync(fileName, json, cancellationToken);

						downloadedChangelists.Add(fixedChangelist);
					}
					catch (Exception e)
					{
						Log.Error(e, $"Failed to save content history changelist for uid={uid}");
					}
				}
			}
			finally
			{
				_contentHistoryChangelistsFileSystemSemaphore.Release();
			}
		}
		catch (OperationCanceledException)
		{
			Log.Debug($"{nameof(SyncContentHistoryChangelists)} was cancelled");
		}

		// Return a page containing the downloaded changelists
		return new ContentHistoryChangelistPage() { Changelists = downloadedChangelists.ToArray() };
	}

	/// <summary>
	/// Loads all locally cached content history changelists from disk, with optional filtering by manifest UIDs.
	/// Files are filtered by filename before deserialization for efficiency.
	/// Changelists are sorted by PublishedAt in ascending order.
	/// Converts all JsonFilePath entries from relative to absolute paths.
	/// </summary>
	/// <param name="pid">The realm id (PID) whose changelists to load</param>
	/// <param name="manifest">The manifest ID, defaults to "global"</param>
	/// <param name="manifestUids">Optional collection of manifest UIDs to filter by (if provided, only matching changelists are loaded)</param>
	/// <returns>
	/// A ContentHistoryChangelistPage containing all matching changelists, sorted by PublishedAt ascending.
	/// All JsonFilePath values in changelist entries are converted to absolute paths.
	/// </returns>
	/// <remarks>
	/// Edge cases:
	/// - Uses semaphore to prevent concurrent file system access
	/// - Retries file reads in a busy-wait loop if IOException occurs (file locked by another process)
	/// - Returns empty page if no changelists match the filters
	/// - Throws if any changelist file fails to deserialize (logs errors for AggregateException)
	/// - All files must have .json extension
	/// - Filename (without extension) is treated as the manifest UID
	/// - Converts relative JsonFilePath to absolute by combining with BeamableWorkspace path
	/// - Processes Created, Modified, and Removed dictionaries independently
	/// </remarks>
	public async Task<ContentHistoryChangelistPage> GetAllContentHistoryLocalChangelists(string pid, string manifest = "global", IEnumerable<string> manifestUids = null)
	{
		// Ensure exclusive access while reading existing changelist files to avoid races with writers
		await _contentHistoryChangelistsFileSystemSemaphore.WaitAsync();
		try
		{
			var localChangelistsPage = new ContentHistoryChangelistPage();
			var changelistsPath = EnsureContentHistoryChangelistsFolderExists(out _, pid, manifest);

			// Convert manifestUids to HashSet for O(1) lookups if provided
			var uidsFilter = manifestUids != null ? new HashSet<string>(manifestUids) : null;

			var tasks =
				// For each existing file inside the changelists folder we care about, we'll have one changelist.
				Directory.EnumerateFiles(changelistsPath)
					// Only get the JSON files
					.Where(fp => Path.GetExtension(fp).EndsWith("json"))
					// Filter by manifest UID based on filename before reading/deserializing
					.Where(fp =>
					{
						if (uidsFilter == null) return true;
						var fileNameWithoutExt = Path.GetFileNameWithoutExtension(fp);
						return uidsFilter.Contains(fileNameWithoutExt);
					})
					// Load the files that passed the filter into memory
					.Select(async fp =>
					{
						// We got to do this so that we don't collide with other software editing the files on disk.
						var text = "";
						var readText = false;
						while (!readText || text == "")
						{
							try
							{
								text = await File.ReadAllTextAsync(fp);
								readText = true;
							}
							catch (IOException)
							{
								readText = false;
								text = "";
							}
						}

						try
						{
							var changelist = JsonSerializer.Deserialize<ContentHistoryChangelist>(text, GetContentFileSerializationOptions());
							return changelist;
						}
						catch (Exception e)
						{
							Log.Error(e, $"Error when loading content history changelist file {fp}");
							throw;
						}
					}).ToList();

			// Wait for all the changelist file tasks and return the list of ContentHistoryChangelist.
			try
			{
				localChangelistsPage.Changelists = (await Task.WhenAll(tasks)).ToArray();
				if (localChangelistsPage.Changelists != null && localChangelistsPage.Changelists.Length > 0)
				{
					// Sort by PublishedAt in ascending order
					System.Array.Sort(localChangelistsPage.Changelists, (c1, c2) => c1.PublishedAt.CompareTo(c2.PublishedAt));

					// Convert relative JsonFilePath to absolute path for each entry
					for (int i = 0; i < localChangelistsPage.Changelists.Length; i++)
					{
						var changelist = localChangelistsPage.Changelists[i];

						void ConvertToAbsolutePath(Dictionary<string, ContentHistoryChangelistEntry> dict)
						{
							if (dict == null) return;
							foreach (string key in dict.Keys)
							{
								var e = dict[key];
								if (!string.IsNullOrEmpty(e.JsonFilePath))
								{
									e.JsonFilePath = Path.Combine(_config.BeamableWorkspace, e.JsonFilePath);
									dict[key] = e;
								}
							}
						}

						ConvertToAbsolutePath(changelist.Created);
						ConvertToAbsolutePath(changelist.Modified);
						ConvertToAbsolutePath(changelist.Removed);

						localChangelistsPage.Changelists[i] = changelist;
					}
				}

				return localChangelistsPage;
			}
			catch (Exception e)
			{
				if (e is AggregateException ae)
				{
					foreach (var aeInnerException in ae.InnerExceptions)
					{
						Log.Error(aeInnerException, "Error when loading content history changelist files");
					}
				}

				throw;
			}
		}
		finally
		{
			_contentHistoryChangelistsFileSystemSemaphore.Release();
		}
	}


	/// <summary>
	/// Loads all locally cached content files from disk for a specific manifest UID, with optional filtering by content IDs.
	/// Retrieves the corresponding changelist to enrich content entries with changelist metadata.
	/// Converts each ContentFile to a ContentHistoryChangelistContentEntry with full metadata.
	/// </summary>
	/// <param name="pid">The realm id (PID) whose content to load</param>
	/// <param name="manifestId">The manifest ID, defaults to "global"</param>
	/// <param name="manifestUid">The specific manifest UID subfolder to load content from (empty string loads from root, but this will likely fail)</param>
	/// <param name="contentIds">Optional collection of content IDs to filter by (if provided, only matching content files are loaded based on filename)</param>
	/// <returns>
	/// A ContentHistoryContentPage containing all matching content entries, each enriched with changelist metadata.
	/// </returns>
	/// <remarks>
	/// Edge cases:
	/// - Uses semaphore to prevent concurrent file system access
	/// - Throws CliException if manifestUid is specified but the directory doesn't exist
	/// - Returns empty page if no changelists exist for the manifestUid
	/// - Only processes the first changelist in the result (assumes one changelist per manifestUid)
	/// - Returns empty page if no content files match the filters
	/// - Filters by content ID based on filename (without extension) before loading
	/// - Searches for changelist entry in Created, Modified, then Removed dictionaries (in that order)
	/// - Converts each ContentFile to ContentHistoryChangelistContentEntry via helper function
	/// - Throws if any content file fails to deserialize (logs errors for AggregateException)
	/// - All files must have .json extension
	/// </remarks>
	public async Task<ContentHistoryContentPage> GetAllContentHistoryLocalContent(string pid, string manifestId = "global", string manifestUid = "", IEnumerable<string> contentIds = null)
	{
		// Ensure exclusive access while reading existing content files to avoid races with writers
		await _contentHistoryContentFileSystemSemaphore.WaitAsync();
		try
		{
			var localContentPage = new ContentHistoryContentPage();

			// If manifestUid is specified, look only in that subfolder
			var contentPath = EnsureContentHistoryContentFolderExists(out _, pid, manifestId);
			var searchPath = string.IsNullOrEmpty(manifestUid) ? contentPath : Path.Combine(contentPath, manifestUid);

			// If we're looking for a specific manifest UID but it doesn't exist, return empty page
			if (!string.IsNullOrEmpty(manifestUid) && !Directory.Exists(searchPath))
			{
				throw new CliException("");
			}

			// Load the changelist for this manifestUid to get the changelist entries
			var changelists = await GetAllContentHistoryLocalChangelists(pid, manifestId, new[] { manifestUid });
			if (changelists.Changelists == null || changelists.Changelists.Length == 0)
			{
				localContentPage.ContentEntries = new List<ContentHistoryChangelistContentEntry>();
				return localContentPage;
			}

			var changelist = changelists.Changelists[0];

			// Convert contentIds to HashSet for O(1) lookups if provided
			var contentIdsFilter = contentIds != null ? new HashSet<string>(contentIds) : null;

			var tasks = new List<Task<(string contentId, ContentFile file)>>();

			// If manifestUid is specified, search in that folder only
			tasks.AddRange(Directory.EnumerateFiles(searchPath)
				.Where(fp => Path.GetExtension(fp).EndsWith("json"))
				// Filter by content ID based on filename before reading/deserializing
				.Where(fp =>
				{
					if (contentIdsFilter == null) return true;
					var fileNameWithoutExt = Path.GetFileNameWithoutExtension(fp);
					return contentIdsFilter.Contains(fileNameWithoutExt);
				})
				.Select(fp => LoadContentFileAsync(fp)));

			// If no tasks, return an empty page
			if (tasks.Count == 0)
			{
				localContentPage.ContentEntries = new List<ContentHistoryChangelistContentEntry>();
				return localContentPage;
			}

			// Wait for all the content file tasks and convert to ContentHistoryChangelistContentEntry
			try
			{
				var contentFiles = await Task.WhenAll(tasks);
				localContentPage.ContentEntries = contentFiles.Select(x =>
				{
					var file = x.file;

					// Find the changelist entry for this content
					ContentHistoryChangelistEntry changelistEntry = default;
					if (changelist.Created?.ContainsKey(file.Id) == true) changelistEntry = changelist.Created[file.Id];
					else if (changelist.Modified?.ContainsKey(file.Id) == true) changelistEntry = changelist.Modified[file.Id];
					else if (changelist.Removed?.ContainsKey(file.Id) == true) changelistEntry = changelist.Removed[file.Id];

					return ContentFileToContentHistoryChangelistContentEntry(file, changelist, changelistEntry);
				}).ToList();
				return localContentPage;
			}
			catch (Exception e)
			{
				if (e is AggregateException ae)
				{
					foreach (var aeInnerException in ae.InnerExceptions)
					{
						Log.Error(aeInnerException, "Error when loading content history content files");
					}
				}

				throw;
			}
		}
		finally
		{
			_contentHistoryContentFileSystemSemaphore.Release();
		}
	}

	/// <summary>
	/// Synchronizes content history content files from the remote Beamable CDN to local storage, downloading only content files that aren't already cached.
	/// Fetches the realm information to get the root PID, loads the corresponding changelist, determines which content files need to be downloaded,
	/// downloads them in parallel from the CDN, and saves them to disk with proper metadata.
	/// </summary>
	/// <param name="pid">The realm id (PID) to sync content for</param>
	/// <param name="manifestId">The manifest ID to sync</param>
	/// <param name="manifestUid">The specific manifest UID whose content files should be synced</param>
	/// <param name="contentIds">The collection of content IDs to sync</param>
	/// <param name="cancellationToken">Cancellation token to abort the sync operation</param>
	/// <returns>A ContentHistoryContentPage containing all content files downloaded during this sync session</returns>
	/// <remarks>
	/// Edge cases:
	/// - Uses semaphore to prevent file system races during concurrent writes
	/// - Creates manifestUid subfolder if it doesn't exist
	/// - Returns empty page if no changelist exists for the manifestUid
	/// - Skips content IDs that already exist locally
	/// - Downloads all content files in parallel using Task.WhenAll
	/// - Handles removed content by using GetContentHistoryContentOldUrl (OldChecksum)
	/// - Handles created/modified content by using GetContentHistoryContentNewUrl (NewChecksum)
	/// - Logs warning if content ID is not found in any dictionary (Created/Modified/Removed)
	/// - Logs errors for individual download/save failures and throws to propagate the exception
	/// - Returns partial results if cancellation occurs mid-sync
	/// - Converts each downloaded ContentFile to ContentHistoryChangelistContentEntry before returning
	/// - Skips entries with empty contentId after download
	/// </remarks>
	public async Task<ContentHistoryContentPage> SyncContentHistoryContent(string pid, string manifestId, string manifestUid, IEnumerable<string> contentIds, CancellationToken cancellationToken = default)
	{
		// Fetch the realm information to get the root PID
		var currentRealmPromise = _realmsApi.GetRealm();

		// Ensure local content folder exists
		var contentPath = EnsureContentHistoryContentFolderExists(out _, pid, manifestId);
		var manifestUuidFolder = Path.Combine(contentPath, manifestUid);
		if (!Directory.Exists(manifestUuidFolder))
		{
			Directory.CreateDirectory(manifestUuidFolder);
		}

		// Convert the enumeration to a HashSet for faster lookups
		var desiredContentIds = new HashSet<string>(contentIds);

		// Load locally cached changelists to find the target changelist
		// If we don't have a changelist for this manifestUid, we can't download content
		var changelists = await GetAllContentHistoryLocalChangelists(pid, manifestId);
		ContentHistoryChangelist targetChangelist = changelists.Changelists.FirstOrDefault(c => c.ManifestUid == manifestUid);
		if (string.IsNullOrEmpty(targetChangelist.ManifestUid)) return new ContentHistoryContentPage() { ContentEntries = new List<ContentHistoryChangelistContentEntry>() };

		// Load locally cached content to exclude files that are already stored
		var allLocalContent = await GetAllContentHistoryLocalContent(pid, manifestId, manifestUid);
		var localContentIds = allLocalContent.ContentEntries.Select(c => c.FullId);

		// Determine which content files need to be downloaded
		var contentIdsToDownload = desiredContentIds.Except(localContentIds);

		// Fetch the Root PID
		var currentRealm = await currentRealmPromise;
		var rootPid = currentRealm.GamePid;

		// Download the content JSON files from the changelist
		var downloadedContent = new List<ContentHistoryChangelistContentEntry>();
		try
		{
			var downloadTasks = contentIdsToDownload.Select(async contentId =>
			{
				try
				{
					// Find the content entry in the changelist
					ContentHistoryChangelistEntry entry = default;
					bool found = false;
					bool isRemoved = false;

					// Check Created dictionary first
					if (targetChangelist.Created != null && targetChangelist.Created.TryGetValue(contentId, out var createdEntry))
					{
						entry = createdEntry;
						found = true;
					}
					// Check Modified dictionary
					else if (targetChangelist.Modified != null && targetChangelist.Modified.TryGetValue(contentId, out var modifiedEntry))
					{
						entry = modifiedEntry;
						found = true;
					}
					// Check Removed dictionary - we CAN download removed content using OldUrl
					else if (targetChangelist.Removed != null && targetChangelist.Removed.TryGetValue(contentId, out var removedEntry))
					{
						entry = removedEntry;
						found = true;
						isRemoved = true;
					}

					if (!found)
					{
						Log.Warning($"Content {contentId} not found in changelist for manifest {manifestUid}");
						return default;
					}

					// Generate the content URL using the appropriate static function
					// For removed entries, use OldUrl; for created/modified entries, use NewUrl
					var uri = isRemoved
						? GetContentHistoryContentOldUrl(_requester.Cid, rootPid, contentId, entry)
						: GetContentHistoryContentNewUrl(_requester.Cid, rootPid, contentId, entry);

					var contentFile = await _requester.CustomRequest<ContentFile>(Method.GET, uri,
						parser: (jsonString) => JsonSerializer.Deserialize<ContentFile>(jsonString, GetContentFileSerializationOptions()));

					contentFile.Id = contentId;
					contentFile.FetchedFromManifestUid = manifestUid;
					contentFile.Tags = JsonSerializer.SerializeToElement(isRemoved ? entry.OldTags : entry.NewTags);
					contentFile.PropertiesChecksum = isRemoved ? entry.OldChecksum : entry.NewChecksum;

					return (contentFile, entry);
				}
				catch (Exception e)
				{
					Log.Error(e, $"Failed to download content history content for contentId={contentId}");
					throw;
				}
			}).ToList();

			var downloadResults = await Task.WhenAll(downloadTasks);

			// Acquire content semaphore while writing files to avoid races with readers/listeners
			await _contentHistoryContentFileSystemSemaphore.WaitAsync(cancellationToken);
			try
			{
				foreach (var (contentFile, changelistEntry) in downloadResults)
				{
					var contentId = contentFile.Id;
					if (string.IsNullOrEmpty(contentId)) continue;

					var fileName = Path.Combine(manifestUuidFolder, $"{contentId}.json");
					try
					{
						var json = JsonSerializer.Serialize(contentFile, GetContentFileSerializationOptions());
						await DirectoryUtils.WriteUtf8FileAsync(fileName, json, cancellationToken);

						// Convert to ContentHistoryChangelistContentEntry
						var entry = ContentFileToContentHistoryChangelistContentEntry(contentFile, targetChangelist, changelistEntry);
						downloadedContent.Add(entry);
					}
					catch (Exception e)
					{
						Log.Error(e, $"Failed to save content history content for contentId={contentId}");
					}
				}
			}
			finally
			{
				_contentHistoryContentFileSystemSemaphore.Release();
			}
		}
		catch (OperationCanceledException)
		{
			Log.Debug($"{nameof(SyncContentHistoryContent)} was cancelled");
		}

		// Return a page containing the downloaded content files
		return new ContentHistoryContentPage() { ContentEntries = downloadedContent };
	}


	/// <summary>
	/// Converts a <see cref="ContentFile"/> to a <see cref="ContentHistoryChangelistContentEntry"/>.
	/// This is a simplified version of <see cref="LocalContentManifestEntry"/> that excludes fields dependent on ReferenceContent (like IsInConflict),
	/// which are not semantically appropriate in the content history.
	/// Uses the changelist entry to determine content status and populate old/new hashes and tags.
	/// </summary>
	public static ContentHistoryChangelistContentEntry ContentFileToContentHistoryChangelistContentEntry(
		ContentFile file,
		ContentHistoryChangelist changelist,
		ContentHistoryChangelistEntry changelistEntry)
	{
		// Determine status based on which dictionary this entry is in
		var status = ContentStatus.UpToDate;
		if (changelist.Created != null && changelist.Created.ContainsKey(file.Id))
		{
			status = ContentStatus.Created;
		}
		else if (changelist.Removed != null && changelist.Removed.ContainsKey(file.Id))
		{
			status = ContentStatus.Deleted;
		}
		else if (changelist.Modified != null && changelist.Modified.ContainsKey(file.Id))
		{
			status = ContentStatus.Modified;
		}

		// Compute tag status using old and new tags from the changelist entry
		var oldTags = changelistEntry.OldTags ?? Array.Empty<string>();
		var newTags = changelistEntry.NewTags ?? Array.Empty<string>();

		var oldTagsSet = new HashSet<string>(oldTags);
		var newTagsSet = new HashSet<string>(newTags);

		// Build a dictionary to look up tag status
		var tagStatusDict = new Dictionary<string, TagStatus>();
		foreach (var tag in newTags)
		{
			if (oldTagsSet.Contains(tag))
			{
				tagStatusDict[tag] = TagStatus.LocalAndRemote; // Tag exists in both old and new
			}
			else
			{
				tagStatusDict[tag] = TagStatus.LocalOnly; // Tag was added in new version
			}
		}

		foreach (var tag in oldTags)
		{
			if (!newTagsSet.Contains(tag))
			{
				tagStatusDict[tag] = TagStatus.RemoteOnly; // Tag was removed in new version
			}
		}

		// Compute TagsStatus array corresponding to NewTags
		var tagsStatus = newTags.Select(tag => (int)tagStatusDict[tag]).ToArray();

		return new ContentHistoryChangelistContentEntry()
		{
			FullId = file.Id,
			TypeName = GetContentType(file),
			Name = GetContentName(file),
			ChangeStatus = (int)status,
			NewHash = changelistEntry.NewChecksum ?? "",
			OldHash = changelistEntry.OldChecksum ?? "",
			NewTags = newTags,
			OldTags = oldTags,
			TagsStatus = tagsStatus,
			JsonFilePath = file.LocalFilePath,
			ReferenceManifestUid = file.FetchedFromManifestUid,
			ChangeDate = changelist.PublishedAt,
		};
	}

