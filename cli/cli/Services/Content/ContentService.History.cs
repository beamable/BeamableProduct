	/// <summary>
	/// Searches for a content history entry within a page by manifest UID.
	/// </summary>
	/// <param name="inPage">The page of entries to search through</param>
	/// <param name="uid">The manifest UID to find</param>
	/// <returns>The matching ContentHistoryEntry, or default(ContentHistoryEntry) if not found</returns>
	/// <remarks>
	/// Edge cases:
	/// - Returns default struct value if no match is found
	/// - Performs linear search (O(n) complexity)
	/// - Assumes ManifestUid is unique within the page (returns first match)
	/// - Does not handle null inPage or null Entries list (will throw NullReferenceException)
	/// </remarks>
	public ContentHistoryEntry GetContentHistoryEntry(ContentHistoryEntriesPage inPage, string uid)
	{
		foreach (var e in inPage.Entries)
		{
			if (e.ManifestUid == uid)
				return e;
		}

		return default;
	}

	/// <summary>
	/// Searches for a content history entry within a page by creation date (Unix timestamp).
	/// Uses binary search since entries are guaranteed to be sorted by CreatedDate in descending order (most recent first).
	/// </summary>
	/// <param name="inPage">The page of entries to search through</param>
	/// <param name="unixTimestamp">The Unix timestamp (milliseconds) of the entry to find</param>
	/// <returns>The matching ContentHistoryEntry, or default(ContentHistoryEntry) if not found</returns>
	/// <remarks>
	/// Edge cases:
	/// - Returns default struct value if no match is found
	/// - Performs binary search (O(log n) complexity)
	/// - Assumes CreatedDate is unique within the page (though collisions are theoretically possible)
	/// - Assumes entries are sorted by CreatedDate in descending order (newest first, oldest last)
	/// - Does not handle null inPage or null Entries list (will throw NullReferenceException)
	/// - Returns default if Entries list is empty
	/// </remarks>
	public ContentHistoryEntry GetContentHistoryEntry(ContentHistoryEntriesPage inPage, long unixTimestamp)
	{
		if (inPage.Entries == null || inPage.Entries.Count == 0)
			return default;

		int left = 0;
		int right = inPage.Entries.Count - 1;

		while (left <= right)
		{
			int mid = left + (right - left) / 2;
			var midEntry = inPage.Entries[mid];

			if (midEntry.CreatedDate == unixTimestamp)
				return midEntry;

			// Descending order: if mid is greater than target, search right half
			if (midEntry.CreatedDate > unixTimestamp)
				left = mid + 1;
			else
				right = mid - 1;
		}

		return default;
	}

	/// <summary>
	/// Loads and deserializes a single content file from disk asynchronously.
	/// Retries reads in a busy-wait loop to avoid collisions with other processes modifying the file.
	/// Extracts the content ID from the filename and populates ContentFile.Id and LocalFilePath.
	/// </summary>
	/// <param name="fp">The full file path to the content JSON file</param>
	/// <returns>A tuple containing the content ID and the deserialized ContentFile</returns>
	/// <remarks>
	/// Edge cases:
	/// - Retries file reads in an infinite loop until successful or text is non-empty (busy-wait on IOException)
	/// - Content ID is derived from filename without extension (e.g., "items.MyItem.json" → "items.MyItem")
	/// - Throws and logs error if deserialization fails
	/// - Does not handle null or invalid file paths (will throw ArgumentException)
	/// - LocalFilePath is set to the full absolute path provided in fp parameter
	/// </remarks>
	private async Task<(string contentId, ContentFile file)> LoadContentFileAsync(string fp)
	{
		// We got to do this so that we don't collide with other software editing the files on disk.
		var text = "";
		var readText = false;
		while (!readText || text == "")
		{
			try
			{
				text = await File.ReadAllTextAsync(fp);
				readText = true;
			}
			catch (IOException)
			{
				readText = false;
				text = "";
			}
		}

		try
		{
			var contentId = Path.GetFileNameWithoutExtension(fp);
			var contentFile = JsonSerializer.Deserialize<ContentFile>(text, GetContentFileSerializationOptions());
			contentFile.Id = contentId;
			contentFile.LocalFilePath = fp;
			return (contentId, contentFile);
		}
		catch (Exception e)
		{
			Log.Error(e, $"Error when loading content history content file {fp}");
			throw;
		}
	}
}

