public struct ChangedContentHistoryEntryFile
{
	public const char ENTRY_FILE_NAME_SEPARATOR = '¬';

	public string OwnerPid;
	public string OwnerManifestId;

	public long OldUnixTimestamp;
	public long UnixTimestamp;

	public string ManifestUid;
	public string OldManifestUid;

