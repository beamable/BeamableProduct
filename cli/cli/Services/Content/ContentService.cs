	public async Task TakeAutoSnapshot(AutoSnapshotType autoSnapshotType, AutoSnapshotActionType snapshotActionType, int maxAutoSavedTempSnapshots, string manifestId = "global")
	{
		if (autoSnapshotType is AutoSnapshotType.LocalOnly or AutoSnapshotType.Both)
		{
			string autoSnapshotTempBaseName = $"AutoSnapshot-{manifestId}-On{snapshotActionType.ToString()}-";
			string tempFolder = GetContentSnapshotDirectoryPath(_config.ConfigDirectoryPath, true);

			if (Path.Exists(tempFolder))
			{
				string[] tempSnapshots = Directory.GetFiles(tempFolder, $"{autoSnapshotTempBaseName}*.json", SearchOption.TopDirectoryOnly);
				if (tempSnapshots.Length >= maxAutoSavedTempSnapshots)
				{
					// Find and delete the older one if we reached the limit of temp snapshots
					string lastFileToDelete = tempSnapshots.OrderBy(item => File.GetLastWriteTime(item).ToFileTime()).First();
					File.Delete(lastFileToDelete);
				}
			}

			var snapshotTakeTime = DateTimeOffset.Now;
			await SnapshotLocalContent($"{autoSnapshotTempBaseName}{snapshotTakeTime:yyyy-MM-ddTHH-mm-ss}", true, manifestId, snapshotTakeTime, true);
		}

		if (autoSnapshotType is AutoSnapshotType.SharedOnly or AutoSnapshotType.Both)
		{
			string sharedFolder = GetContentSnapshotDirectoryPath(_config.ConfigDirectoryPath, false);
			string autoSnapshotSharedName = $"LastPublished-{manifestId}.json";
			string lastPublishedSnapshotFullPath = Path.Combine(sharedFolder, autoSnapshotSharedName);
			// Make sure to delete the old published snapshot
			if (File.Exists(lastPublishedSnapshotFullPath))
			{
				File.Delete(lastPublishedSnapshotFullPath);
			}

			await SnapshotLocalContent(autoSnapshotSharedName, false, manifestId, isAutoSnapshot:true);
		}

	}

	public async Task<string> SnapshotLocalContent(string snapshotName, bool saveLocal, string manifestId = "global", DateTimeOffset? snapshotDate = null, bool isAutoSnapshot = false)
	{
		if (Path.GetInvalidFileNameChars().Any(snapshotName.Contains))
		{
			throw new CliException($"Snapshot name: {snapshotName} contains invalid file name characters.");
		}

		long snapshotTimestamp = snapshotDate?.ToUnixTimeMilliseconds() ?? DateTimeOffset.Now.ToUnixTimeMilliseconds();
		
		// Get Folder to save the snapshot
		string folderPath = GetContentSnapshotDirectoryPath(_config.ConfigDirectoryPath, saveLocal);

		// Ensure that the snapshot folder path exists
		Directory.CreateDirectory(folderPath);

		snapshotName = Path.GetExtension(snapshotName) == ".json" ? snapshotName : $"{snapshotName}.json";
		string snapshotFilePath = Path.Combine(folderPath, snapshotName);
		if (File.Exists(snapshotFilePath))
			throw new CliException($"Manifest with name {snapshotName} already exist in folder {folderPath}, please use another name for this snapshot or delete it");
		
		// Get content folder to the snapshot
		var contentFolder = EnsureContentPathForRealmExists(out _, _requester.Pid, manifestId);
		var parseContentFiles = Directory.EnumerateFiles(contentFolder).Select(async fp =>
		{
			// We got to do this so that we don't collide with other software editing the files on disk. 
			string text = "";
			bool readText = false;
			while (!readText || text == "")
			{
				try
				{
					text = await File.ReadAllTextAsync(fp);
					readText = true;
				}
				catch (IOException)
				{
					readText = false;
					text = "";
				}
			}

			try
			{
				// Deserialize ContentFile to pass it's values to the new ContentFileSnapshot.
				var contentFile = JsonSerializer.Deserialize<ContentFile>(text, GetContentFileSerializationOptions());

				var properties = contentFile.Properties;
				
				var contentFileSnapshot = new ContentFileSnapshot()
				{
					Properties = properties,
					Tags = contentFile.Tags,
					Checksum = CalculateChecksum(in contentFile),
				};
				string id = Path.GetFileNameWithoutExtension(fp);
				return (id, contentFile: contentFileSnapshot);
			}
			catch (Exception e)
			{
				throw new CliException($"Error deserializing content {fp}. ErrorMessage: {e.Message}");
			}
		});

		try
		{
			var contentFiles = await Task.WhenAll(parseContentFiles);
			var currentUser = await _accountsApi.GetAdminMe();
			var currentRealm = await _realmsApi.GetRealm();
			var contentSnapshot = new ManifestSnapshot()
			{
				ContentFiles = contentFiles.ToDictionary(item => item.id, item => item.contentFile),
				SnapshotTimestamp = snapshotTimestamp,
				ProjectData = new ManifestProjectData() { PID = currentRealm.Pid, RealmName = currentRealm.DisplayName },
				Author = new ManifestAuthor() {AccountId = currentUser.id, Email = currentUser.email},
				ManifestId = manifestId,
				IsAutoSnapshot = isAutoSnapshot,
			};
			string serializedSnapshotContent = JsonSerializer.Serialize(contentSnapshot, GetContentFileSerializationOptions());
			await DirectoryUtils.WriteUtf8FileAsync(snapshotFilePath, serializedSnapshotContent);
		}
		catch (Exception e)
		{
			Log.Error($"Error when saving snapshot, please check log for more details. ErrorMessage: {e.Message}");
			throw;
		}

		return snapshotFilePath;
	}

