using System.Globalization;
using System.Text;
using System.Text.RegularExpressions;

namespace cli.Services.Analytics;

public record GeneratedAnalyticsFile(string FileName, string Content);

/// <summary>
/// Emits one Unreal C++ header per enabled analytics event. Each header defines a USTRUCT
/// deriving from FBeamAnalyticsEvent with overrides for GetOpCode / GetCategory /
/// GetEventName plus a Validate(FBeamValidationContext&) override that calls into the
/// UBeamValidators static library. Consumed by the `beam analytics generate-validators` command.
/// </summary>
public class AnalyticsValidatorGenerator
{
	public List<GeneratedAnalyticsFile> Generate(List<AnalyticsEventSchema> events, string apiPrefix = "")
	{
		var result = new List<GeneratedAnalyticsFile>();
		foreach (var ev in events)
		{
			if (!ev.Enabled) continue;
			result.Add(GenerateOne(ev, apiPrefix));
		}
		return result;
	}

	private GeneratedAnalyticsFile GenerateOne(AnalyticsEventSchema ev, string apiPrefix)
	{
		var typePascal = ToPascalCase(ev.Name);
		var structName = $"F{apiPrefix}{typePascal}Event";
		var schema = ev.Schema;

		var opCode = string.IsNullOrEmpty(schema.BeamOpCode) ? "g.core" : schema.BeamOpCode!;
		var category = schema.BeamCategory ?? "";
		var schemaVersion = string.IsNullOrEmpty(schema.SchemaVersion) ? "1.0.0" : schema.SchemaVersion!;

		var fields = schema.Properties
			.Select(kvp => MapField(kvp.Key, kvp.Value, schema.Required.Contains(kvp.Key)))
			.ToList();

		var sb = new StringBuilder();
		sb.AppendLine("// Auto-generated by BeamableCLI - DO NOT EDIT");
		sb.AppendLine($"// Source event: {ev.Name}");
		sb.AppendLine($"// Schema version: {schemaVersion}");
		if (!string.IsNullOrEmpty(ev.Description))
		{
			sb.AppendLine($"// Description: {EscapeForComment(ev.Description)}");
		}
		sb.AppendLine();
		sb.AppendLine("#pragma once");
		sb.AppendLine();
		sb.AppendLine("#include \"CoreMinimal.h\"");
		sb.AppendLine("#include \"Analytics/BeamAnalyticsEvent.h\"");
		sb.AppendLine("#include \"Runtime/BeamValidators.h\"");
		sb.AppendLine();
		sb.AppendLine($"#include \"{structName.Substring(1)}.generated.h\"");
		sb.AppendLine();
		sb.AppendLine("USTRUCT(BlueprintType, meta=(BeamAnalyticsEventBase))");
		sb.AppendLine($"struct {structName} : public FBeamAnalyticsEvent");
		sb.AppendLine("{");
		sb.AppendLine("\tGENERATED_BODY()");
		sb.AppendLine();

		foreach (var field in fields)
		{
			sb.AppendLine("\tUPROPERTY(EditAnywhere, BlueprintReadWrite, Category=\"Analytics\")");
			sb.AppendLine($"\t{field.CppType} {field.CppName}{field.DefaultInit};");
			sb.AppendLine();
		}

		sb.AppendLine($"\t{structName}() {{ Version = TEXT(\"{EscapeString(schemaVersion)}\"); }}");
		sb.AppendLine();
		sb.AppendLine($"\tvirtual FString GetOpCode()    const override {{ return TEXT(\"{EscapeString(opCode)}\"); }}");
		sb.AppendLine($"\tvirtual FString GetCategory()  const override {{ return TEXT(\"{EscapeString(category)}\"); }}");
		sb.AppendLine($"\tvirtual FString GetEventName() const override {{ return TEXT(\"{EscapeString(ev.Name)}\"); }}");
		sb.AppendLine();

		sb.AppendLine("\tvirtual void Validate(FBeamValidationContext& OutContext) const override");
		sb.AppendLine("\t{");

		bool first = true;
		foreach (var field in fields)
		{
			if (!first) sb.AppendLine();
			first = false;
			AppendValidateBlock(sb, field);
		}

		sb.AppendLine("\t}");
		sb.AppendLine();

		AppendSerializeProperties(sb, fields, "TUnrealJsonSerializer");
		sb.AppendLine();
		AppendSerializeProperties(sb, fields, "TUnrealPrettyJsonSerializer");
		sb.AppendLine();
		AppendDeserializeProperties(sb, fields);

		sb.AppendLine("};");

		return new GeneratedAnalyticsFile($"{structName.Substring(1)}.h", sb.ToString());
	}

	private static FieldInfo MapField(string jsonName, JsonSchemaProperty prop, bool required)
	{
		var pascal = ToPascalCase(jsonName);
		string cppType, cppName, defaultInit;

		switch (prop.Type)
		{
			case "integer":
				cppType = "int32";
				cppName = pascal;
				defaultInit = " = 0";
				break;
			case "number":
				cppType = "float";
				cppName = pascal;
				defaultInit = " = 0.0f";
				break;
			case "boolean":
				cppType = "bool";
				cppName = "b" + pascal;
				defaultInit = " = false";
				break;
			case "string":
			default:
				cppType = "FString";
				cppName = pascal;
				defaultInit = "";
				break;
		}

		return new FieldInfo
		{
			JsonName = jsonName,
			CppName = cppName,
			CppType = cppType,
			DefaultInit = defaultInit,
			Property = prop,
			Required = required,
		};
	}

	private static void AppendSerializeProperties(StringBuilder sb, List<FieldInfo> fields, string serializerType)
	{
		sb.AppendLine($"\tvirtual void BeamSerializeProperties({serializerType}& Serializer) const override");
		sb.AppendLine("\t{");
		sb.AppendLine("\t\tFBeamAnalyticsEvent::BeamSerializeProperties(Serializer);");
		foreach (var field in fields)
		{
			sb.AppendLine($"\t\tSerializer->WriteValue(TEXT(\"{EscapeString(field.CppName)}\"), {field.CppName});");
		}
		sb.AppendLine("\t}");
	}

	private static void AppendDeserializeProperties(StringBuilder sb, List<FieldInfo> fields)
	{
		sb.AppendLine("\tvirtual void BeamDeserializeProperties(const TSharedPtr<FJsonObject>& Bag) override");
		sb.AppendLine("\t{");
		sb.AppendLine("\t\tFBeamAnalyticsEvent::BeamDeserializeProperties(Bag);");
		foreach (var field in fields)
		{
			var jsonKey = $"TEXT(\"{EscapeString(field.CppName)}\")";
			string getter;
			switch (field.Property.Type)
			{
				case "integer":
					getter = $"Bag->GetIntegerField({jsonKey})";
					break;
				case "number":
					getter = $"(float)Bag->GetNumberField({jsonKey})";
					break;
				case "boolean":
					getter = $"Bag->GetBoolField({jsonKey})";
					break;
				case "string":
				default:
					getter = $"Bag->GetStringField({jsonKey})";
					break;
			}

			sb.AppendLine($"\t\tif (Bag->HasField({jsonKey}))");
			sb.AppendLine($"\t\t\t{field.CppName} = {getter};");
		}
		sb.AppendLine("\t}");
	}

	private static void AppendValidateBlock(StringBuilder sb, FieldInfo field)
	{
		var resultVar = $"{field.CppName}Result";
		sb.AppendLine($"\t\tFBeamValidationResult {resultVar}(TEXT(\"{EscapeString(field.CppName)}\"));");
		sb.AppendLine($"\t\t{resultVar}.SchemaPath = TEXT(\"properties.{EscapeString(field.JsonName)}\");");

		var p = field.Property;
		switch (p.Type)
		{
			case "integer":
				if (p.Minimum.HasValue)
					sb.AppendLine($"\t\tUBeamValidators::ValidateMinimum({field.CppName}, {IntLit(p.Minimum.Value)}, {resultVar});");
				if (p.Maximum.HasValue)
					sb.AppendLine($"\t\tUBeamValidators::ValidateMaximum({field.CppName}, {IntLit(p.Maximum.Value)}, {resultVar});");
				if (p.ExclusiveMinimum.HasValue)
					sb.AppendLine($"\t\tUBeamValidators::ValidateExclusiveMinimum({field.CppName}, {IntLit(p.ExclusiveMinimum.Value)}, {resultVar});");
				if (p.ExclusiveMaximum.HasValue)
					sb.AppendLine($"\t\tUBeamValidators::ValidateExclusiveMaximum({field.CppName}, {IntLit(p.ExclusiveMaximum.Value)}, {resultVar});");
				if (p.MultipleOf.HasValue)
					sb.AppendLine($"\t\tUBeamValidators::ValidateMultipleOf({field.CppName}, {IntLit(p.MultipleOf.Value)}, {resultVar});");
				break;

			case "number":
				if (p.Minimum.HasValue)
					sb.AppendLine($"\t\tUBeamValidators::ValidateMinimum({field.CppName}, {FloatLit(p.Minimum.Value)}, {resultVar});");
				if (p.Maximum.HasValue)
					sb.AppendLine($"\t\tUBeamValidators::ValidateMaximum({field.CppName}, {FloatLit(p.Maximum.Value)}, {resultVar});");
				if (p.ExclusiveMinimum.HasValue)
					sb.AppendLine($"\t\tUBeamValidators::ValidateExclusiveMinimum({field.CppName}, {FloatLit(p.ExclusiveMinimum.Value)}, {resultVar});");
				if (p.ExclusiveMaximum.HasValue)
					sb.AppendLine($"\t\tUBeamValidators::ValidateExclusiveMaximum({field.CppName}, {FloatLit(p.ExclusiveMaximum.Value)}, {resultVar});");
				if (p.MultipleOf.HasValue)
					sb.AppendLine($"\t\tUBeamValidators::ValidateMultipleOf({field.CppName}, {FloatLit(p.MultipleOf.Value)}, {resultVar});");
				break;

			case "string":
				if (field.Required)
					sb.AppendLine($"\t\tUBeamValidators::ValidateRequired({field.CppName}, {resultVar});");
				if (p.MinLength.HasValue)
					sb.AppendLine($"\t\tUBeamValidators::ValidateMinLength({field.CppName}, {p.MinLength.Value}, {resultVar});");
				if (p.MaxLength.HasValue)
					sb.AppendLine($"\t\tUBeamValidators::ValidateMaxLength({field.CppName}, {p.MaxLength.Value}, {resultVar});");
				if (!string.IsNullOrEmpty(p.Pattern))
					sb.AppendLine($"\t\tUBeamValidators::ValidatePattern({field.CppName}, TEXT(\"{EscapeString(p.Pattern!)}\"), {resultVar});");
				break;

			case "boolean":
			default:
				// Boolean has no JSON Schema constraints beyond type — value-typed in C++, always valid.
				break;
		}

		sb.AppendLine($"\t\tOutContext.RecordResult({resultVar});");
	}

	// ── Helpers ──────────────────────────────────────────────────────────────

	private static string IntLit(double value) =>
		((int)value).ToString(CultureInfo.InvariantCulture);

	private static string FloatLit(double value)
	{
		var s = value.ToString("R", CultureInfo.InvariantCulture);
		// C++ float literals need a decimal point or exponent before the trailing 'f'.
		if (!s.Contains('.') && !s.Contains('e') && !s.Contains('E')) s += ".0";
		return s + "f";
	}

	private static string EscapeString(string s) =>
		s.Replace("\\", "\\\\").Replace("\"", "\\\"");

	private static string EscapeForComment(string s) =>
		s.Replace("\r", " ").Replace("\n", " ");

	private static string ToPascalCase(string input)
	{
		if (string.IsNullOrEmpty(input)) return input;
		var words = Regex.Split(input, @"(?<=[a-z0-9])(?=[A-Z])|[\s\-_\.]+")
			.Where(w => !string.IsNullOrEmpty(w));
		var sb = new StringBuilder();
		foreach (var w in words)
		{
			sb.Append(char.ToUpperInvariant(w[0]));
			if (w.Length > 1) sb.Append(w.Substring(1));
		}
		return sb.ToString();
	}

	private class FieldInfo
	{
		public string JsonName = "";
		public string CppName = "";
		public string CppType = "";
		public string DefaultInit = "";
		public JsonSchemaProperty Property = new();
		public bool Required;
	}
}
