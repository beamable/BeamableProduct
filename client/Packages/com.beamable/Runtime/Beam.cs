		/// <summary>
		/// Runs the <see cref="BeamContext.ClearPlayerAndStop"/> method on every <see cref="BeamContext"/> in memory.
		/// </summary>
		public static async Promise ClearAndStopAllContexts()
		{
			foreach (var ctx in BeamContext.All)
			{
				await ctx.ClearPlayerAndStop();
			}
		}

		/// <summary>
		/// Runs the <see cref="BeamContext.Stop"/> method on every <see cref="BeamContext"/> in memory.
		/// </summary>
		public static async Promise StopAllContexts()
		{
			foreach (var ctx in BeamContext.All)
			{
				await ctx.Stop();
			}
		}

		/// <summary>
		/// Completely stop Beamable, including the <see cref="GlobalScope"/> and all <see cref="BeamContext"/>s.
		/// Then, reloads the <see cref="GlobalScope"/> but does not reload any <see cref="BeamContext"/>s.
		/// <param name="sceneQualifier">The string should either be a scene name, or the stringified int of a scene build index. If null is given, the scene is not reloaded.</param>
		/// </summary>
		public static async Promise RestartBeamable(string sceneQualifier = "0")
		{
			await StopAllContexts();
			await _globalProviderScope.Dispose();

			GlobalScope = _globalProviderScope = GlobalDependencyBuilder.Build();

			if (!string.IsNullOrEmpty(sceneQualifier))
			{
				await ResetToScene(sceneQualifier);
			}
		}

		/// <summary>
		/// Changes the current PID value for the game, and resets the game to the given scene defined by <see cref="sceneQualifier"/>.
		/// When the next <see cref="BeamContext"/> loads, it will use the same CID as before, but the
		/// PID will be the value given to this function.
		/// </summary>
		/// <remarks>
		/// If the passed PID is the same as the current one no action is performed.
		/// </remarks> 
		/// <param name="pid">a valid Beamable PID for the current CID.</param>
		/// <param name="sceneQualifier">The string should either be a scene name, or the stringified int of a scene build index.</param>
		/// <returns><see cref="SwitchPidResult"/> An enum with information if PID change was performed.</returns>
		public static async Promise<SwitchPidResult> SwitchToPid(string pid, string sceneQualifier = "0")
		{
			if (RuntimeConfigProvider.Pid.Equals(pid))
				return SwitchPidResult.NoAction;
			if (!DefaultRuntimeConfigProvider.IsValidPid(pid))
				return SwitchPidResult.InvalidPidNoAction;

			await StopAllContexts();
			RuntimeConfigProvider.Pid = pid;
			await ResetToScene(sceneQualifier);
			return SwitchPidResult.SwitchedToNewPid;
		}

		/// <summary>
		/// Changes the current PID value for the game, and resets the game to the given scene defined by <see cref="sceneQualifier"/>.
		/// When the next <see cref="BeamContext"/> loads, it will use the same CID as before, but the
		/// PID will be the value given to this function.
		/// </summary>
		/// <param name="pid">a valid Beamable PID for the current CID.</param>
		/// <param name="sceneQualifier">The string should either be a scene name, or the stringified int of a scene build index.</param>
		[Obsolete("Method is obsolete, please use SwitchToPid instead.")]
		public static async Promise ChangePid(string pid, string sceneQualifier = "0")
		{
			if (!DefaultRuntimeConfigProvider.IsValidPid(pid))
			{
				Debug.LogError($"PID {pid} is not valid.");
				return;
			}
			await StopAllContexts();
			RuntimeConfigProvider.Pid = pid;
			await ResetToScene(sceneQualifier);
		}

		/// <summary>
		/// Unload every scene, and reload some requested scene.
		/// If no <paramref name="sceneQualifier"/> is given, then this will reload the current scene.
		/// If you are in the Unity Editor, this means the scene will reload to the state when you entered Playmode.
		/// Otherwise, if you are in a built game, this will reload the scene as it exists in the Project Build Settings.
		///
		/// This method won't deploy objects in the DontDestroyOnLoad scene.
		/// </summary>
		/// <param name="sceneQualifier">The string should either be a scene name, or the stringified int of a scene build index.</param>
		public static async Promise ResetToScene(string sceneQualifier = null)
		{
			var loadAction = GetLoadSceneFunction(sceneQualifier);

			var totalScenesRequired = 0;
			var scenesDestroyed = 0;
			var allScenesUnloaded = new Promise();

			Action<AsyncOperation> Check(Scene unloadedScene)
			{
				return (_) =>
				{
					scenesDestroyed++;
					if (scenesDestroyed != totalScenesRequired) return;

					allScenesUnloaded.CompleteSuccess();
				};
			}

			var autoScene = SceneManager.CreateScene("_autogeneratored_" + Guid.NewGuid().ToString());
			totalScenesRequired = SceneManager.sceneCount - 1;
			for (var i = 0; i < totalScenesRequired; i++)
			{
				var toDestroy = SceneManager.GetSceneAt(i);
				if (autoScene == toDestroy) continue;
				var op = SceneManager.UnloadSceneAsync(toDestroy);
				if (op == null)
				{
					Check(toDestroy)(null);
				}
				else op.completed += Check(toDestroy);
			}

			await allScenesUnloaded;
			loadAction();
		}

		private static Action GetLoadSceneFunction(string sceneQualifier = null)
		{
			Action loadAction = () =>
			{
				Debug.LogWarning("No scene could be identified to reload.");
			};
			if (sceneQualifier == null)
			{
				// load the activeScene, or if in Unity Editor, load
#if UNITY_EDITOR
				loadAction = () =>
				{
					UnityEditor.SceneManagement.EditorSceneManager.LoadSceneInPlayMode(
						"Temp/__Backupscenes/0.backup", new LoadSceneParameters(LoadSceneMode.Single));
				};
#else
				var activeScene = SceneManager.GetActiveScene();
				loadAction = () =>
				{
					SceneManager.LoadScene(activeScene.name, LoadSceneMode.Single);
				};
#endif
			}
			else if (int.TryParse(sceneQualifier, out var buildIndex))
			{
				loadAction = () =>
				{
					SceneManager.LoadScene(buildIndex, LoadSceneMode.Single);
				};
			}
			else
			{
				loadAction = () =>
				{
					SceneManager.LoadScene(sceneQualifier, LoadSceneMode.Single);
				};
			}

			return loadAction;
		}
	}

	/// <summary>
	/// Return type of the <see cref="Beam.SwitchToPid"/> method.
	/// </summary>
	public enum SwitchPidResult
	{
		/// <summary>
		/// The PID passed to the method was the same as the active PID and no action was performed.
		/// </summary>
		NoAction,
		/// <summary>
		/// The PID passed to the method was in an incorrect format, and no action was performed.
		/// The correct format for a PID is `DE_` followed by numbers.
		/// </summary>
		InvalidPidNoAction,
		/// <summary>
		/// Beam stopped all <see cref="BeamContext"/> instances and switched to the new PID.
		/// </summary>
		SwitchedToNewPid,
		
	}
