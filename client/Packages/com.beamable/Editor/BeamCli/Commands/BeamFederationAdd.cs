
namespace Beamable.Editor.BeamCli.Commands
{
	using Beamable.Common;
	using Beamable.Common.BeamCli;

	public partial class FederationAddArgs : Beamable.Common.BeamCli.IBeamCommandArgs
	{
		/// <summary>The BeamoId of the microservice to add</summary>
		public string microservice;
		/// <summary>A federation id. This uniquely identifies your federation. Different federation interfaces can be associated with each id</summary>
		public string fedId;
		/// <summary>The type of federation to add</summary>
		public Beamable.Api.Autogenerated.Models.FederationType fedType;
		/// <summary>Serializes the arguments for command line usage.</summary>
		public virtual string Serialize()
		{
			// Create a list of arguments for the command
			System.Collections.Generic.List<string> genBeamCommandArgs = new System.Collections.Generic.List<string>();
			// Add the microservice value to the list of args.
			genBeamCommandArgs.Add(this.microservice.ToString());
			// Add the fedId value to the list of args.
			genBeamCommandArgs.Add(this.fedId.ToString());
			// Add the fedType value to the list of args.
			genBeamCommandArgs.Add(this.fedType.ToString());
			string genBeamCommandStr = "";
			// Join all the args with spaces
			genBeamCommandStr = string.Join(" ", genBeamCommandArgs);
			return genBeamCommandStr;
		}
	}
	public partial class BeamCommands
	{
		public virtual FederationAddWrapper FederationAdd(FederationAddArgs addArgs)
		{
			// Create a list of arguments for the command
			System.Collections.Generic.List<string> genBeamCommandArgs = new System.Collections.Generic.List<string>();
			genBeamCommandArgs.Add("beam");
			genBeamCommandArgs.Add(defaultBeamArgs.Serialize());
			genBeamCommandArgs.Add("federation");
			genBeamCommandArgs.Add("add");
			genBeamCommandArgs.Add(addArgs.Serialize());
			// Create an instance of an IBeamCommand
			Beamable.Common.BeamCli.IBeamCommand command = this._factory.Create();
			// Join all the command paths and args into one string
			string genBeamCommandStr = string.Join(" ", genBeamCommandArgs);
			// Configure the command with the command string
			command.SetCommand(genBeamCommandStr);
			FederationAddWrapper genBeamCommandWrapper = new FederationAddWrapper();
			genBeamCommandWrapper.Command = command;
			// Return the command!
			return genBeamCommandWrapper;
		}
	}
	public partial class FederationAddWrapper : Beamable.Common.BeamCli.BeamCommandWrapper
	{
		public virtual FederationAddWrapper OnStreamAddFederationCommandOutput(System.Action<ReportDataPoint<BeamAddFederationCommandOutput>> cb)
		{
			this.Command.On("stream", cb);
			return this;
		}
	}
}
